// ─── Public API ─────────────────────────────────────────────────────────────────

/** Lower an IR module to a LeanAST file. */
export function lowerModule(mod: IRModule): LeanFile {
  const ctx = new LowerCtx();
  ctx.collectStructInfo(mod.decls);
  ctx.collectDefinedNames(mod.decls);
  // Track names imported from external modules (not resolvable in Lean)
  for (const imp of mod.imports) {
    if (imp.module.startsWith('TSLean.External.') && imp.names) {
      for (const n of imp.names) ctx.externalImportNames.add(n);
    }
  }

  const decls: LeanDecl[] = [];

  // Imports
  const imports = ctx.resolveImports(mod);
  for (const imp of imports) {
    decls.push({ tag: 'Import', module: imp });
  }
  decls.push({ tag: 'Blank' });

  // Open namespaces
  const opens = ctx.resolveOpens(mod, imports);
  decls.push({ tag: 'Open', namespaces: opens });
  decls.push({ tag: 'Blank' });

  // Namespace
  const ns = mod.name;
  const useNs = ns && ns !== 'T' && ns !== 'Test';
  if (useNs) {
    // Missing state structs (registers them in structFields as empty, then enriches)
    const missingStructs = ctx.emitMissingStateStructs(mod.decls);
    // Main declarations with mutual detection
    const bodyDecls = [...missingStructs, ...ctx.lowerDeclsWithMutual(mod.decls)];
    decls.push({ tag: 'Namespace', name: ns, decls: bodyDecls });
  } else {
    const missingStructs = ctx.emitMissingStateStructs(mod.decls);
    for (const d of missingStructs) decls.push(d);
    for (const d of ctx.lowerDeclsWithMutual(mod.decls)) decls.push(d);
  }

  return {
    banner: 'Auto-generated by ts-lean-transpiler',
    sourcePath: mod.sourceFile ?? 'unknown',
    decls,
  };
}

