/**
 * @module codegen/lower
 *
 * Lowering pass: IR → LeanAST.
 *
 * Translates the transpiler's IR (typed, effect-annotated) into a Lean 4
 * surface syntax AST that the printer can emit directly. All semantic
 * decisions happen here:
 *   - do vs pure (based on Effect annotations, not string matching)
 *   - partial vs total (based on recursive self-reference detection)
 *   - sorry for unrepresentable constructs (TS API boundary, etc.)
 *   - sanitization of identifiers (Lean keywords)
 *   - import resolution (scanning the AST for needed modules)
 *   - mutual block detection (transitive cross-references between types)
 *
 * Pipeline:  IR → Rewrite → **Lowering** → Printer → Text
 */

