function sorryForType(t?: IRType): LeanExpr {
  if (!t) return { tag: 'Sorry' };
  switch (t.tag) {
    case 'Bool': return { tag: 'Sorry', ty: { tag: 'TyName', name: 'Bool' } };
    case 'String': return { tag: 'Sorry', ty: { tag: 'TyName', name: 'String' } };
    case 'Nat': return { tag: 'Sorry', ty: { tag: 'TyName', name: 'Nat' } };
    case 'Int': return { tag: 'Sorry', ty: { tag: 'TyName', name: 'Int' } };
    case 'Float': return { tag: 'Sorry', ty: { tag: 'TyName', name: 'Float' } };
    case 'Unit': return { tag: 'Lit', value: '()' };
    case 'Option': return { tag: 'None' };
    case 'Array': return { tag: 'ArrayLit', elems: [] };
    case 'TypeRef': return { tag: 'Sorry', ty: { tag: 'TyName', name: t.name } };
    default: return { tag: 'Sorry' };
  }
}

function defaultForType(t: IRType): LeanExpr {
  switch (t.tag) {
    case 'Nat': return { tag: 'Lit', value: '0' };
    case 'Int': return { tag: 'Lit', value: '0' };
    case 'Float': return { tag: 'TypeAnnot', expr: { tag: 'Lit', value: '0' }, ty: { tag: 'TyName', name: 'Float' } };
    case 'String': return { tag: 'Lit', value: '""' };
    case 'Bool': return { tag: 'Lit', value: 'false' };
    case 'Unit': return { tag: 'Lit', value: '()' };
    case 'Array': return { tag: 'ArrayLit', elems: [] };
    case 'Option': return { tag: 'None' };
    default: return { tag: 'Default' };
  }
}

function needsParens(e: IRExpr): boolean {
  return e.tag === 'App' || e.tag === 'BinOp' || e.tag === 'UnOp' ||
         e.tag === 'IfThenElse' || e.tag === 'Lambda' || e.tag === 'Let' ||
         e.tag === 'Bind' || e.tag === 'TryCatch' || e.tag === 'Match' ||
         e.tag === 'LitFloat';
}

function isEmptyElse(e: IRExpr): boolean {
  if (e.tag === 'LitUnit') return true;
  if (e.tag === 'Pure_' && e.value.tag === 'LitUnit') return true;
  return false;
}

function fixStateEffect(eff: Effect, params: IRParam[]): Effect {
  const selfParam = params.find(p => p.name === 'self');
  if (!selfParam) return eff;
  if (eff.tag === 'State' && eff.stateType.tag === 'Unit')
    return { tag: 'State', stateType: selfParam.type };
  if (eff.tag === 'Combined')
    return { tag: 'Combined', effects: eff.effects.map(e =>
      e.tag === 'State' && e.stateType.tag === 'Unit' ? { tag: 'State' as const, stateType: selfParam.type } : e) };
  return eff;
}

function bodyContainsVarRef(expr: IRExpr, name: string): boolean {
  function check(e: IRExpr): boolean {
    if (e.tag === 'Var' && e.name === name) return true;
    switch (e.tag) {
      case 'App': return check(e.fn) || e.args.some(check);
      case 'Lambda': return check(e.body);
      case 'Let': return check(e.value) || check(e.body);
      case 'Bind': return check(e.monad) || check(e.body);
      case 'IfThenElse': return check(e.cond) || check(e.then) || check(e.else_);
      case 'Match': return check(e.scrutinee) || e.cases.some(c => check(c.body));
      case 'Sequence': return e.stmts.some(check);
      case 'BinOp': return check(e.left) || check(e.right);
      case 'UnOp': return check(e.operand);
      case 'FieldAccess': return check(e.obj);
      case 'IndexAccess': return check(e.obj) || check(e.index);
      case 'Assign': return check(e.target) || check(e.value);
      case 'TryCatch': return check(e.body) || check(e.handler);
      case 'Throw': return check(e.error);
      case 'Return': return check(e.value);
      case 'Await': return check(e.expr);
      case 'Cast': return check(e.expr);
      case 'CtorApp': return e.args.some(check);
      case 'ArrayLit': return e.elems.some(check);
      case 'TupleLit': return e.elems.some(check);
      case 'StructLit': return e.fields.some(f => check(f.value));
      default: return false;
    }
  }
  return check(expr);
}

function bodyContainsAnyVarRef(expr: IRExpr, names: Set<string>, selfName: string): boolean {
  function check(e: IRExpr): boolean {
    if (!e || typeof e !== 'object') return false;
    if (e.tag === 'Var' && names.has(e.name) && e.name !== selfName) return true;
    for (const v of Object.values(e)) {
      if (v && typeof v === 'object') {
        if (Array.isArray(v)) {
          for (const item of v) {
            if (item && typeof item === 'object' && 'tag' in item && check(item as IRExpr)) return true;
            if (item && typeof item === 'object') {
              for (const sub of Object.values(item)) {
                if (sub && typeof sub === 'object' && 'tag' in (sub as any) && check(sub as IRExpr)) return true;
              }
            }
          }
        } else if ('tag' in v) {
          if (check(v as IRExpr)) return true;
        }
      }
    }
    return false;
  }
  return check(expr);
}

function collectFieldAccesses(expr: IRExpr, varName: string): Map<string, IRType> {
  const fields = new Map<string, IRType>();
  const methodCalls = new Set<string>();
  function walk(e: IRExpr): void {
    if (!e || typeof e !== 'object') return;
    if (e.tag === 'App' && e.fn.tag === 'FieldAccess' && e.fn.obj.tag === 'Var' && e.fn.obj.name === varName)
      methodCalls.add(e.fn.field);
    if (e.tag === 'FieldAccess' && e.obj.tag === 'Var' && e.obj.name === varName)
      fields.set(e.field, e.type);
    for (const v of Object.values(e)) {
      if (v && typeof v === 'object') {
        if (Array.isArray(v)) {
          for (const item of v) {
            if (item && typeof item === 'object' && 'tag' in item) walk(item as IRExpr);
            if (item && typeof item === 'object') {
              for (const sub of Object.values(item)) {
                if (sub && typeof sub === 'object' && 'tag' in (sub as any)) walk(sub as IRExpr);
              }
            }
          }
        } else if ('tag' in v) walk(v as IRExpr);
      }
    }
  }
  walk(expr);
  for (const m of methodCalls) fields.delete(m);
  return fields;
}

function flattenConcat(e: IRExpr): IRExpr[] | null {
  if (e.tag === 'LitString') return [e];
  if (e.tag === 'BinOp' && (e.op === 'Concat' || (e.op === 'Add' && e.left?.type?.tag === 'String'))) {
    const left = flattenConcat(e.left);
    const right = flattenConcat(e.right);
    if (!left || !right) return null;
    return [...left, ...right];
  }
  if (isSafeInterp(e)) return [e];
  return null;
}

function isSafeInterp(e: IRExpr): boolean {
  switch (e.tag) {
    case 'LitString': case 'LitNat': case 'LitFloat': case 'LitBool': case 'Var': return true;
    case 'FieldAccess': return isSafeInterp(e.obj);
    case 'Cast': return isSafeInterp(e.expr);
    default: return false;
  }
}

// ─── Constraint → type class mapping ────────────────────────────────────────────

/**
 * Map a TypeScript generic constraint (IRType) to Lean type class names.
 *
 * Tier 1 (primitive): string→ToString, number→OfNat/OfScientific, boolean→DecidableEq
 * Tier 2 (named interface/class): Comparable→Comparable, Serializable→Serializable
 * Tier 3 (structural): { name: string } → HasField "name" String (deferred)
 */
function constraintToTypeClasses(constraint: IRType): string[] {
  switch (constraint.tag) {
    case 'String':  return ['ToString'];
    case 'Nat':     return ['OfNat'];
    case 'Int':     return ['OfNat'];
    case 'Float':   return ['OfScientific'];
    case 'Bool':    return ['DecidableEq'];
    case 'TypeRef':
      // Named interface/class constraint: <T extends Comparable> → [Comparable T]
      return [constraint.name];
    case 'TypeVar':
      // <T extends U> where U is another type param — not directly expressible
      return [];
    default:
      return [];
  }
}


