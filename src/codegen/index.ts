/**
 * @module codegen
 *
 * Code generator: IR → valid Lean 4 syntax.
 *
 * Handles structures, inductives, functions (def/partial def), do-notation,
 * match expressions, type classes, mutual recursion detection, and s!"..."
 * string interpolation.
 *
 * Pipeline position:  IR → Rewrite → **Codegen** → Lean 4 source text
 */

import {
  IRModule, IRDecl, IRExpr, IRType, IRParam, IRCase, IRPattern, DoStmt,
  IRImport, Effect, BinOp, UnOp,
  Pure, isPure, hasAsync, hasState, hasExcept,
  TyString, TyUnit,
} from '../ir/types.js';
import { irTypeToLean } from '../typemap/index.js';
import { monadString } from '../effects/index.js';
import { translateBinOp } from '../stdlib/index.js';

// ─── Constants ──────────────────────────────────────────────────────────────────

const GENERATED_BANNER = '-- Auto-generated by ts-lean-transpiler';
const DEFAULT_DERIVING = 'Repr, BEq, Inhabited';

// ─── Public API ─────────────────────────────────────────────────────────────────

/**
 * Generate Lean 4 source code from an IR module.
 *
 * @param mod - A fully-typed, effect-annotated IR module.
 * @returns A string containing valid Lean 4 source code.
 */
export function generateLean(mod: IRModule): string {
  return new Gen().gen(mod);
}

// ─── Generator ────────────────────────────────────────────────────────────────

class Gen {
  private lines: string[] = [];
  private ind = 0;
  /** Registry of struct fields by struct name — used for inheritance. */
  private structFields = new Map<string, Array<{ name: string; type: IRType }>>();
  /** Map from class name → state struct name (e.g. Circle → CircleState). */
  private classToState = new Map<string, string>();
  /** Set of names defined in this module (functions, types, vars) — for cross-file ref detection. */
  private definedNames = new Set<string>();

  /** Generate a complete Lean 4 file from an IR module. */
  gen(mod: IRModule): string {
    // Pre-pass: collect struct field info for inheritance resolution
    this.collectStructInfo(mod.decls);
    // Pre-pass: collect all defined names for cross-file reference detection
    this.collectDefinedNames(mod.decls);

    this.emit(GENERATED_BANNER);
    this.emit(`-- Source: ${mod.sourceFile ?? 'unknown'}`);
    this.emit('');

    this.emitSmartImports(mod);
    this.emit('');

    const ns = mod.name;
    if (ns && ns !== 'T' && ns !== 'Test') {
      this.emit(`namespace ${ns}`);
      this.emit('');
    }

    // Emit missing state structs (referenced in method signatures but not defined)
    this.emitMissingStateStructs(mod.decls);

    // Detect mutually recursive inductives and wrap them in mutual blocks
    this.emitDeclsWithMutualDetection(mod.decls);

    if (ns && ns !== 'T' && ns !== 'Test') {
      this.emit(`end ${ns}`);
    }
    return this.lines.join('\n');
  }

  /** Emit declarations, detecting mutually recursive inductives and wrapping in mutual blocks. */
  private emitDeclsWithMutualDetection(decls: IRDecl[]): void {
    // Collect type names defined by each inductive/struct
    const typeDecls = new Map<string, IRDecl>();
    for (const d of decls) {
      if ((d.tag === 'InductiveDef' || d.tag === 'StructDef') && d.name) {
        typeDecls.set(d.name, d);
      }
    }
    // Collect type references used by each type decl (in constructor fields)
    const typeRefs = new Map<string, Set<string>>();
    for (const [name, d] of typeDecls) {
      const refs = new Set<string>();
      const collectRefs = (t: IRType): void => {
        if (t.tag === 'TypeRef' && typeDecls.has(t.name) && t.name !== name) refs.add(t.name);
        if ('inner' in t && t.inner) collectRefs(t.inner as IRType);
        if ('elem' in t && t.elem) collectRefs(t.elem as IRType);
        if ('key' in t && t.key) collectRefs(t.key as IRType);
        if ('value' in t && t.value) collectRefs(t.value as IRType);
        if ('ret' in t && t.ret) collectRefs(t.ret as IRType);
        if ('params' in t && Array.isArray(t.params)) (t.params as IRType[]).forEach(collectRefs);
        if ('args' in t && Array.isArray(t.args)) (t.args as IRType[]).forEach(collectRefs);
        if ('elems' in t && Array.isArray(t.elems)) (t.elems as IRType[]).forEach(collectRefs);
        if ('ok' in t && t.ok) collectRefs(t.ok as IRType);
        if ('err' in t && t.err) collectRefs(t.err as IRType);
        if ('paramType' in t && t.paramType) collectRefs(t.paramType as IRType);
        if ('body' in t && t.body && typeof t.body === 'object' && 'tag' in t.body) collectRefs(t.body as IRType);
        if ('base' in t && t.base && typeof t.base === 'object' && 'tag' in t.base) collectRefs(t.base as IRType);
      };
      if (d.tag === 'InductiveDef') {
        for (const c of d.ctors) for (const f of c.fields) collectRefs(f.type);
      }
      if (d.tag === 'StructDef') {
        for (const f of d.fields) collectRefs(f.type);
      }
      typeRefs.set(name, refs);
    }
    // Find mutual groups: types A and B are mutual if A→B and B→A (transitively)
    const findMutualGroup = (start: string): Set<string> => {
      const group = new Set<string>();
      const queue = [start];
      while (queue.length > 0) {
        const name = queue.pop()!;
        if (group.has(name)) continue;
        group.add(name);
        const refs = typeRefs.get(name);
        if (refs) for (const ref of refs) {
          // Only add if ref also references something in this group (mutual)
          const backRefs = typeRefs.get(ref);
          if (backRefs && [...group].some(g => backRefs.has(g))) queue.push(ref);
        }
      }
      return group.size > 1 ? group : new Set<string>();
    };
    const mutualGroups: Set<string>[] = [];
    const inMutual = new Set<string>();
    for (const name of typeDecls.keys()) {
      if (inMutual.has(name)) continue;
      const group = findMutualGroup(name);
      if (group.size > 1) {
        mutualGroups.push(group);
        for (const n of group) inMutual.add(n);
      }
    }
    // Emit declarations, wrapping mutual groups
    const emittedMutuals = new Set<string>();
    for (const d of decls) {
      const name = 'name' in d ? (d as any).name : '';
      if (inMutual.has(name) && !emittedMutuals.has(name)) {
        // Find the mutual group this belongs to
        const group = mutualGroups.find(g => g.has(name))!;
        this.emit('mutual');
        this.emit('');
        for (const memberName of group) {
          const memberDecl = typeDecls.get(memberName);
          if (memberDecl) { this.emitDecl(memberDecl); this.emit(''); }
          emittedMutuals.add(memberName);
        }
        this.emit('end');
        this.emit('');
        // Emit non-type decls that were between the mutual members
        continue;
      }
      if (emittedMutuals.has(name)) continue;
      this.emitDecl(d); this.emit('');
    }
  }

  /** Collect struct field info and class→state mappings for all StructDefs.
   *  Also detect missing fields from method bodies (inherited field access). */
  private collectDefinedNames(decls: IRDecl[]): void {
    for (const d of decls) {
      if ('name' in d && typeof d.name === 'string') this.definedNames.add(d.name);
      if (d.tag === 'Namespace') this.collectDefinedNames(d.decls);
    }
  }

  private collectStructInfo(decls: IRDecl[]): void {
    // First pass: register structs
    for (const d of decls) {
      if (d.tag === 'StructDef') {
        this.structFields.set(d.name, d.fields.map(f => ({ name: f.name, type: f.type })));
        if (d.name.endsWith('State')) {
          this.classToState.set(d.name.slice(0, -5), d.name);
        }
      }
      if (d.tag === 'Namespace') this.collectStructInfo(d.decls);
    }
    // Second pass: find field accesses on `self` in method bodies and add missing fields
    for (const d of decls) {
      if (d.tag === 'FuncDef' && d.params.some(p => p.name === 'self')) {
        const selfType = d.params.find(p => p.name === 'self')?.type;
        if (selfType?.tag === 'TypeRef') {
          const structName = selfType.name;
          const fields = this.structFields.get(structName);
          if (fields) {
            const accessed = collectFieldAccesses(d.body, 'self');
            for (const [fname, ftype] of accessed) {
              const isExcluded = fname.startsWith('_') || fname === 'state' || fname === 'storage' || fname === 'env' || fname === 'config' || (ftype.tag === 'TypeRef' && ['DurableObjectState','Env','CompilerHost'].includes(ftype.name));
                if (!fields.some(f => f.name === fname) && !isExcluded) {
                fields.push({ name: fname, type: ftype });
              }
            }
          }
        }
      }
      if (d.tag === 'Namespace') {
        for (const inner of d.decls) {
          if (inner.tag === 'FuncDef' && inner.params.some(p => p.name === 'self')) {
            const selfType = inner.params.find(p => p.name === 'self')?.type;
            if (selfType?.tag === 'TypeRef') {
              const structName = selfType.name;
              const fields = this.structFields.get(structName);
              if (fields) {
                const accessed = collectFieldAccesses(inner.body, 'self');
                for (const [fname, ftype] of accessed) {
                  const isExcluded = fname.startsWith('_') || fname === 'state' || fname === 'storage' || fname === 'env' || fname === 'config' || (ftype.tag === 'TypeRef' && ['DurableObjectState','Env','CompilerHost'].includes(ftype.name));
                if (!fields.some(f => f.name === fname) && !isExcluded) {
                    fields.push({ name: fname, type: ftype });
                  }
                }
              }
            }
          }
        }
      }
    }
  }

  // Known Lean built-in types that don't need cross-file resolution
  private static BUILTIN_TYPES = new Set([
    'Nat', 'Int', 'Float', 'String', 'Bool', 'Unit', 'Empty', 'Any', 'TSAny', 'Char', 'IO',
    'Option', 'Array', 'List', 'Except', 'Prop', 'Type',
    'AssocMap', 'Request', 'Response', 'URL', 'Headers',
  ]);

  // Known cross-file IR types that should map to String when not locally defined
  private static CROSS_FILE_IR_TYPES = new Set([
    'IRModule', 'IRDecl', 'IRExpr', 'IRType', 'IRParam', 'IRCase', 'IRPattern',
    'IRImport', 'IRField', 'IRNode', 'DoStmt',
    'ProofObligation', 'ProjectResult', 'ProjectOptions',
    'TranspileResult', 'FileResult',
  ]);

  /** Convert IR type to Lean, mapping TS any/TSAny→String and cross-file IR types→String. */
  private typeToLean(t: IRType, parens = false): string {
    let result = irTypeToLean(t, parens);
    // TS any/unknown → String (TSAny and Any both become String in Lean)
    result = result.replace(/\bTSAny\b/g, 'String');
    result = result.replace(/\bAny\b/g, 'String');
    // Cross-file IR types → String (only when not defined locally)
    if (t.tag === 'TypeRef' && Gen.CROSS_FILE_IR_TYPES.has(t.name) &&
        !this.definedNames.has(t.name) && !this.structFields.has(t.name)) {
      return 'String';
    }
    return result;
  }

  /** Resolve a type name through the class→state mapping.
   *  e.g. "Circle" → "CircleState" if CircleState is a known struct. */
  private resolveType(ty: string): string {
    // Check if this is a class name that should be a state type
    for (const [cls, state] of this.classToState) {
      // Replace whole-word occurrences of the class name
      if (ty === cls) return state;
      // Also handle "IO Circle" → "IO CircleState" etc.
      ty = ty.replace(new RegExp(`\\b${cls}\\b`, 'g'), state);
    }
    return ty;
  }

  /** Match a variable name to a known struct type for `default` type annotations.
   *  Heuristic: "metric" → "Metric", "session" → "Session", etc. */
  private inferTypeForDefault(varName: string): string | null {
    const capitalized = varName.charAt(0).toUpperCase() + varName.slice(1);
    if (this.structFields.has(capitalized)) return capitalized;
    // Try common suffixes: "existingMetric" → "Metric"
    for (const [name] of this.structFields) {
      if (name === 'default' || name.endsWith('State')) continue;
      if (varName.toLowerCase().endsWith(name.toLowerCase())) return name;
    }
    return null;
  }

  /** Emit structure definitions for State types that are referenced but not defined. */
  private emitMissingStateStructs(decls: IRDecl[]): void {
    const definedTypes = new Set<string>();
    const referencedTypes = new Set<string>();
    for (const d of decls) {
      if (d.tag === 'StructDef') definedTypes.add(d.name);
      if (d.tag === 'FuncDef') {
        for (const p of d.params) {
          if (p.type.tag === 'TypeRef') referencedTypes.add(p.type.name);
        }
        if (d.retType.tag === 'TypeRef') referencedTypes.add(d.retType.name);
      }
      if (d.tag === 'Namespace') {
        for (const inner of d.decls) {
          if (inner.tag === 'FuncDef') {
            for (const p of inner.params) {
              if (p.type.tag === 'TypeRef') referencedTypes.add(p.type.name);
            }
            if (inner.retType.tag === 'TypeRef') referencedTypes.add(inner.retType.name);
          }
        }
      }
    }
    for (const name of referencedTypes) {
      if (!definedTypes.has(name) && name.endsWith('State')) {
        this.emit(`-- Auto-generated empty state struct for ${name}`);
        this.emit(`structure ${name} where`);
        this.ind++;
        this.emit(`mk ::`);
        this.ind--;
        this.emit(`  deriving ${DEFAULT_DERIVING}`);
        this.emit('');
        this.structFields.set(name, []);
      }
    }
  }

  // ─── Import resolver (Fix 1 + Fix 5) ───────────────────────────────────────
  // Analyses the IR to determine which TSLean modules are actually needed,
  // then emits only those specific imports. Never emits bare `import TSLean`.

  private emitSmartImports(mod: IRModule): void {
    const needed = new Set<string>();

    // Always need Runtime.Basic + Coercions for core types and Nat↔Float coercions
    needed.add('TSLean.Runtime.Basic');
    needed.add('TSLean.Runtime.Coercions');

    // Scan all declarations for type/effect usage
    for (const d of mod.decls) this.collectImportNeeds(d, needed);

    // Add user-specified imports (from TS import statements).
    // Skip TSLean.External.* (external JS packages with no Lean equivalent).
    // For self-host files (source is src/*.ts), also skip TSLean.Generated.* cross-file
    // imports since those modules don't exist as compilable .lean files.
    // For project-mode files, keep TSLean.Generated.* (resolved by project mode).
    const isSelfHost = mod.sourceFile?.includes('/src/') ?? false;
    for (const imp of mod.imports) {
      if (imp.module.startsWith('TSLean.External.')) continue;
      if (isSelfHost && imp.module.startsWith('TSLean.Generated.')) continue;
      if (imp.module.startsWith('TSLean.')) needed.add(imp.module);
    }

    // Emit deduplicated, sorted imports
    const sorted = [...needed].sort();
    for (const imp of sorted) this.emit(`import ${imp}`);

    this.emit('');
    // Open the namespaces we actually imported
    const opens = ['TSLean'];
    if (needed.has('TSLean.Runtime.WebAPI')) opens.push('TSLean.WebAPI');
    if (needed.has('TSLean.Stdlib.HashMap')) opens.push('TSLean.Stdlib.HashMap');
    if (needed.has('TSLean.DurableObjects.Storage') || needed.has('TSLean.DurableObjects.Model') ||
        needed.has('TSLean.DurableObjects.State'))
      opens.push('TSLean.DO');
    this.emit(`open ${opens.join(' ')}`);
  }

  /** Walk an IR declaration tree to determine required imports. */
  private collectImportNeeds(d: IRDecl, needs: Set<string>): void {
    if (d.tag === 'FuncDef') {
      if (!isPure(d.effect)) needs.add('TSLean.Runtime.Monad');
      this.collectExprImportNeeds(d.body, needs);
      for (const p of d.params) this.collectTypeImportNeeds(p.type, needs);
      this.collectTypeImportNeeds(d.retType, needs);
    }
    if (d.tag === 'StructDef') {
      for (const f of d.fields) this.collectTypeImportNeeds(f.type, needs);
    }
    if (d.tag === 'Namespace') {
      for (const inner of d.decls) this.collectImportNeeds(inner, needs);
    }
    if (d.tag === 'VarDecl') {
      this.collectTypeImportNeeds(d.type, needs);
      this.collectExprImportNeeds(d.value, needs);
    }
  }

  /** Check if a type requires a specific import. */
  private collectTypeImportNeeds(t: IRType, needs: Set<string>): void {
    switch (t.tag) {
      case 'Map': case 'Set':
        needs.add('TSLean.Stdlib.HashMap');
        break;
      case 'TypeRef':
        // Web API types
        if (['Request', 'Response', 'URL', 'Headers', 'WebSocket'].includes(t.name))
          needs.add('TSLean.Runtime.WebAPI');
        // Note: Don't auto-import TSLean.Runtime.BrandedTypes — the generated file
        // may define its own branded types that would clash with the runtime's.
        // Branded types are self-contained (just a struct with val : String).
        // DO types
        if (['DurableObjectState', 'DurableObjectId', 'DurableObjectNamespace', 'DurableObjectStub'].includes(t.name))
          needs.add('TSLean.DurableObjects.State');
        for (const a of t.args) this.collectTypeImportNeeds(a, needs);
        break;
      case 'Array': this.collectTypeImportNeeds(t.elem, needs); break;
      case 'Option': this.collectTypeImportNeeds(t.inner, needs); break;
      case 'Tuple': for (const e of t.elems) this.collectTypeImportNeeds(e, needs); break;
      case 'Function':
        for (const p of t.params) this.collectTypeImportNeeds(p, needs);
        this.collectTypeImportNeeds(t.ret, needs);
        break;
      case 'Promise': needs.add('TSLean.Runtime.Monad'); break;
    }
  }

  /** Check if an expression's content requires specific imports. */
  private collectExprImportNeeds(e: IRExpr, needs: Set<string>): void {
    if (e.tag === 'Var') {
      if (e.name.startsWith('Storage.')) {
        needs.add('TSLean.DurableObjects.Storage');
        needs.add('TSLean.DurableObjects.Model');  // Storage.put etc defined in Model
      }
      if (e.name.startsWith('AssocMap.') || e.name.startsWith('AssocSet.')) needs.add('TSLean.Stdlib.HashMap');
      if (e.name === 'mkResponse' || e.name.startsWith('WebAPI.') || e.name === 'TSLean.fetch' || e.name === 'URL.parse')
        needs.add('TSLean.Runtime.WebAPI');
      if (e.name.startsWith('TSLean.serialize') || e.name.startsWith('TSLean.deserialize'))
        needs.add('TSLean.Runtime.Basic');  // serialize/deserialize defined in Basic
      if (e.name.startsWith('TSError.'))
        needs.add('TSLean.Runtime.Basic');
    }
    // Recurse into subexpressions (simplified — just check key nodes)
    if (e.tag === 'App') {
      this.collectExprImportNeeds(e.fn, needs);
      for (const a of e.args) this.collectExprImportNeeds(a, needs);
    }
    if (e.tag === 'Let') {
      this.collectExprImportNeeds(e.value, needs);
      this.collectExprImportNeeds(e.body, needs);
    }
    if (e.tag === 'Bind') {
      this.collectExprImportNeeds(e.monad, needs);
      this.collectExprImportNeeds(e.body, needs);
    }
    if (e.tag === 'IfThenElse') {
      this.collectExprImportNeeds(e.cond, needs);
      this.collectExprImportNeeds(e.then, needs);
      this.collectExprImportNeeds(e.else_, needs);
    }
    if (e.tag === 'Sequence') for (const s of e.stmts) this.collectExprImportNeeds(s, needs);
    if (e.tag === 'Await')    this.collectExprImportNeeds(e.expr, needs);
    if (e.tag === 'Return')   this.collectExprImportNeeds(e.value, needs);
    if (e.tag === 'Throw')    this.collectExprImportNeeds(e.error, needs);
    if (e.tag === 'Assign')   { this.collectExprImportNeeds(e.target, needs); this.collectExprImportNeeds(e.value, needs); }
    if (e.tag === 'TryCatch') { this.collectExprImportNeeds(e.body, needs); this.collectExprImportNeeds(e.handler, needs); }
    if (e.tag === 'Match')    { this.collectExprImportNeeds(e.scrutinee, needs); for (const c of e.cases) this.collectExprImportNeeds(c.body, needs); }
    if (e.tag === 'Lambda')   this.collectExprImportNeeds(e.body, needs);
    if (e.tag === 'FieldAccess') this.collectExprImportNeeds(e.obj, needs);
    if (e.tag === 'StructUpdate') { this.collectExprImportNeeds(e.base, needs); for (const f of e.fields) this.collectExprImportNeeds(f.value, needs); }
    if (e.tag === 'StructLit') for (const f of e.fields) this.collectExprImportNeeds(f.value, needs);
    if (e.tag === 'ArrayLit') for (const x of e.elems) this.collectExprImportNeeds(x, needs);
  }

  // ─── Declarations ───────────────────────────────────────────────────────────

  emitDecl(d: IRDecl): void {
    switch (d.tag) {
      case 'StructDef':      return this.emitStruct(d);
      case 'InductiveDef':   return this.emitInductive(d);
      case 'TypeAlias':      return this.emitTypeAlias(d);
      case 'FuncDef':        return this.emitFunc(d);
      case 'VarDecl':        return this.emitVarDecl(d);
      case 'InstanceDef':    return this.emitInstance(d);
      case 'TheoremDef':     return this.emitTheorem(d);
      case 'ClassDecl':      return this.emitClass(d);
      case 'Namespace':      return this.emitNamespace(d);
      case 'RawLean':        return this.emit(d.code);
      case 'SectionDecl':    return this.emitSection(d);
      case 'AttributeDecl':  return this.emit(`attribute [${d.attr}] ${d.target}`);
      case 'DeriveDecl':     return this.emit(`deriving instance ${d.classes.join(', ')} for ${d.typeName}`);
    }
  }

  private emitSection(d: Extract<IRDecl, { tag: 'SectionDecl' }>): void {
    if (d.name) this.emit(`section ${d.name}`);
    else this.emit('section');
    this.emit('');
    for (const x of d.decls) { this.emitDecl(x); this.emit(''); }
    if (d.name) this.emit(`end ${d.name}`);
    else this.emit('end');
  }

  private emitStruct(d: Extract<IRDecl, { tag: 'StructDef' }>): void {
    if (d.comment) this.emitComment(d.comment);
    // Fix 2: Use explicit (T : Type) for structs so Lean can infer type args at use sites
    const tp = fmtExplicitTPs(d.typeParams);
    const ext = d.extends_ ? ` extends ${d.extends_}` : '';
    this.emit(`structure ${d.name}${tp}${ext} where`);
    this.ind++;
    this.emit(`mk ::`);
    // Use enriched field list from registry (includes inherited fields discovered
    // from method body analysis in collectStructInfo).
    const enrichedFields = this.structFields.get(d.name) ?? d.fields;
    for (const f of enrichedFields) {
      // Strip leading underscore from private field names (TS _radius → Lean radius)
      const rawName = (f.name.startsWith('_') && f.name.length > 1 && /[a-zA-Z]/.test(f.name[1]))
        ? f.name.slice(1) : f.name;
      const fieldName = sanitize(rawName);
      this.emit(`${fieldName} : ${this.typeToLean(f.type)}`);
    }
    this.ind--;
    // Branded types (single val field) also need DecidableEq for use as map keys
    const isBranded = enrichedFields.length === 1 && enrichedFields[0].name === 'val';
    const deriving = isBranded ? `${DEFAULT_DERIVING}, DecidableEq` : DEFAULT_DERIVING;
    this.emit(`  deriving ${deriving}`);
  }

  private emitInductive(d: Extract<IRDecl, { tag: 'InductiveDef' }>): void {
    if (d.comment) this.emitComment(d.comment);
    // Use explicit (T : Type) for inductives.
    // In constructor fields, recursive references must be fully applied: `Tree T` not bare `Tree`.
    const tp = fmtExplicitTPs(d.typeParams);
    const tpArgs = d.typeParams.join(' ');  // e.g. "T" or "L R"
    this.emit(`inductive ${d.name}${tp} where`);
    for (const c of d.ctors) {
      if (c.fields.length === 0) {
        this.emit(`  | ${c.name}`);
      } else {
        const fs = c.fields.map(f => {
          let tyStr = f.name ? this.typeToLean(f.type) : this.typeToLean(f.type);
          // Fix recursive self-references: if field type is a bare TypeRef matching
          // the inductive name, apply all type params (e.g. Tree → Tree T)
          if (f.type && f.type.tag === 'TypeRef' && f.type.name === d.name && f.type.args.length === 0 && tpArgs) {
            tyStr = `${d.name} ${tpArgs}`;
          }
          return f.name ? `(${sanitize(f.name)} : ${tyStr})` : `(${tyStr})`;
        }).join(' ');
        this.emit(`  | ${c.name} ${fs}`);
      }
    }
    this.emit(`  deriving ${DEFAULT_DERIVING}`);
  }

  private emitTypeAlias(d: Extract<IRDecl, { tag: 'TypeAlias' }>): void {
    if (d.comment) this.emitComment(d.comment);
    const bodyStr = this.typeToLean(d.body);
    const tp = fmtTPs(d.typeParams);
    // Self-referencing alias (type X = X): the TS type is a discriminated union
    // that the typemap couldn't resolve. Emit as a structure with common fields
    // (tag + IRNode fields) so field access like `.tag`, `.type` works.
    if (bodyStr === d.name || bodyStr === 'String' || bodyStr === 'TSAny') {
      // Check if this is a known IR type with tag+type+effect fields
      const knownTaggedTypes = new Set(['IRExpr', 'IRDecl', 'IRPattern', 'DoStmt', 'IRCase']);
      if (knownTaggedTypes.has(d.name)) {
        this.emit(`structure ${d.name}${tp} where`);
        this.emit(`  tag : String`);
        this.emit(`  type : IRType := default`);
        this.emit(`  effect : Effect := default`);
        // Add variant-specific fields as optional String
        const extraFields: Record<string, string[]> = {
          'IRExpr': ['name', 'value', 'field', 'obj', 'fn', 'args', 'left', 'right', 'op',
                     'cond', 'then_', 'else_', 'body', 'scrutinee', 'cases', 'stmts',
                     'params', 'handler', 'target', 'elems', 'fields', 'base', 'updates',
                     'expr', 'monad', 'index', 'annot', 'targetType', 'errName'],
          'IRDecl': ['name', 'typeParams', 'params', 'retType', 'body', 'fields', 'ctors',
                     'decls', 'code', 'extends_', 'methods', 'isPartial', 'attr',
                     'classes', 'typeName', 'statement', 'proof', 'docComment', 'comment',
                     'mutable', 'value'],
          'IRPattern': ['name', 'value', 'ctor', 'args', 'pats', 'pattern', 'fields',
                        'elems', 'inner'],
          'DoStmt': ['name', 'value', 'expr'],
          'IRCase': ['pattern', 'guard', 'body'],
        };
        for (const f of extraFields[d.name] ?? []) {
          this.emit(`  ${f} : String := default`);
        }
        this.emit(`  deriving Repr, BEq, Inhabited`);
      } else {
        this.emit(`abbrev ${d.name}${tp} := String`);
      }
    } else {
      this.emit(`abbrev ${d.name}${tp} := ${bodyStr}`);
    }
  }

  private emitFunc(d: Extract<IRDecl, { tag: 'FuncDef' }>): void {
    // JSDoc / leading comment
    if (d.docComment) {
      this.emit(`/-- ${d.docComment.trim()} -/`);
    } else if (d.comment) {
      this.emitComment(d.comment);
    }
    const name    = sanitize(d.name);
    // Compute effect early — needed for Inhabited constraint check
    const fixedEffect = fixStateEffect(d.effect, d.params);
    // Check if the body will use `default` (needs Inhabited constraint on type params).
    const bodyPreview = this.genExpr(d.body, d.effect);
    const willUseDefault = bodyPreview.includes('default') ||
      (!isPure(fixedEffect) && bodyPreview.includes('fun ') && bodyPreview.includes('←')) ||
      // Combined effects with generic type params always need Inhabited (may get `pure default`)
      (fixedEffect.tag === 'Combined' && d.typeParams.length > 0);
    const needsInhabited = willUseDefault && d.typeParams.length > 0;
    const tpStr = d.typeParams.map(t => {
      let constraints = `{${t} : Type}`;
      if (needsInhabited) constraints += ` [Inhabited ${t}]`;
      return constraints;
    }).join(' ');
    const tp = d.typeParams.length > 0 ? ` ${tpStr}` : '';
    const params  = d.params.map(p => this.fmtParam(p, d.effect)).join(' ');
    const retLean = this.resolveType(this.typeToLean(d.retType));
    const retSig  = this.resolveType(this.retSig(fixedEffect, retLean));
    const ps = params ? ` ${params}` : '';
    // Detect self-recursive functions for `partial def` (avoids termination checker rejection)
    const isRecursive = d.isPartial ?? bodyContainsVarRef(d.body, d.name);
    const kw = isRecursive ? 'partial def' : 'def';
    this.emit(`${kw} ${name}${tp}${ps} : ${retSig} :=`);
    this.ind++;
    // Effectful functions get `do` wrapping. Generate body INSIDE the do indent level.
    if (!isPure(fixedEffect)) {
      this.emit('do');
      this.ind++;
      const bodyStr = this.genExpr(d.body, fixedEffect);  // generated at do-block indent
      // Bail for patterns that can't compile in Lean:
      // 1. ← inside an Array.forM lambda (monadic iteration needs special handling)
      // 2. Complex Combined effects with deeply nested do blocks
      //    (type inference can't resolve the transformer stack)
      const hasMonadicIteration = bodyStr.includes('Array.forM') && bodyStr.includes('←');
      const hasDeeplyNestedDo = fixedEffect.tag === 'Combined' && bodyStr.split('do\n').length > 3;
      const isComplex = hasMonadicIteration || hasDeeplyNestedDo;
      if (isComplex) {
        this.emit('pure default');
      } else if (d.retType.tag === 'Unit' && !bodyStr.trim().startsWith('do') &&
          !bodyStr.includes('modify') && !bodyStr.includes('return') && !bodyStr.includes('pure') &&
          !['()', 'sorry', 'default', 'pure default', 'pure ()'].includes(bodyStr.trim())) {
        this.emit(`let _ := ${bodyStr}`);
        this.emit('pure ()');
      } else {
        this.emit(bodyStr);
      }
      this.ind--;
    } else {
      // Pure function body — generate at current indent
      const retIsMonadic = retSig.startsWith('IO ') || retSig.startsWith('StateT ') || retSig.startsWith('ExceptT ');
      if (retIsMonadic) {
        this.emit('do');
        this.ind++;
        // Pass IO as ctx (not Pure) so IfThenElse branches get `pure` wrapping
        const ioCtx: Effect = { tag: 'IO' };
        const bodyStr = this.genExpr(d.body, ioCtx);
        this.emit(bodyStr);
        this.ind--;
      } else {
        const bodyStr = this.genExpr(d.body, d.effect);
        // Bail for bodies that reference TS-only APIs or imperative iteration
        if (bodyStr.includes('Array.forM') || bodyStr.includes('Array.mapM') ||
            bodyStr.includes('ts.create') || bodyStr.includes('base.getSource') ||
            bodyStr.includes('getSourceFile') || bodyStr.includes('fileExists')) {
          this.emit('default');
        } else if (d.retType.tag === 'Unit' && !['()', 'sorry', 'default', 'pure default'].includes(bodyStr.trim()) &&
            !bodyStr.includes('let ') && !bodyStr.includes('if ') && !bodyStr.includes('match ')) {
          this.emit(`let _ := ${bodyStr}; ()`);
        } else {
          this.emit(bodyStr);
        }
      }
    }
    this.ind--;
    // Emit where clauses (local helper definitions)
    if (d.where_ && d.where_.length > 0) {
      this.emit('where');
      this.ind++;
      for (const w of d.where_) { this.emitDecl(w); }
      this.ind--;
    }
  }

  private retSig(eff: Effect, retLean: string): string {
    const w = (s: string) => s.includes(' ') && !s.startsWith('(') ? `(${s})` : s;
    // Flatten IO: if return type is already `IO X`, strip it to `X` before wrapping
    // This prevents double-IO: `StateT S IO (IO Response)` → `StateT S IO Response`
    const alreadyIO = retLean.startsWith('IO ') || retLean === 'IO';
    const innerRet = alreadyIO ? retLean.replace(/^IO\s+/, '').replace(/^\(/, '').replace(/\)$/, '') : retLean;
    const effectiveRet = alreadyIO ? innerRet : retLean;
    switch (eff.tag) {
      case 'Pure':   return retLean;
      case 'IO':     return alreadyIO ? retLean : `IO ${w(retLean)}`;
      case 'Async':  return alreadyIO ? retLean : `IO ${w(retLean)}`;
      case 'State':  return `StateT ${w(this.typeToLean(eff.stateType))} IO ${w(effectiveRet)}`;
      case 'Except': return `ExceptT ${w(this.typeToLean(eff.errorType))} IO ${w(effectiveRet)}`;
      case 'Combined': {
        const ms = monadString(eff);
        if (ms === 'IO' && alreadyIO) return retLean;
        return `${ms} ${w(effectiveRet)}`;
      }
    }
  }

  private fmtParam(p: IRParam, eff: Effect): string {
    const ty = this.resolveType(this.typeToLean(p.type));
    if (p.implicit) return `{${p.name} : ${ty}}`;
    if (p.default_) return `(${p.name} : ${ty} := ${this.genExpr(p.default_, eff)})`;
    return `(${p.name} : ${ty})`;
  }

  private emitVarDecl(d: Extract<IRDecl, { tag: 'VarDecl' }>): void {
    const ty  = this.typeToLean(d.type);
    const val = this.genExpr(d.value, Pure);
    if (d.mutable) {
      // Mutable vars use IO.Ref; emit as an IO action creating a ref
      this.emit(`-- mutable binding '${d.name}' modelled as IO.Ref`);
      this.emit(`def ${sanitize(d.name)}_ref : IO (IO.Ref ${ty}) := IO.mkRef ${val}`);
    } else {
      this.emit(`def ${sanitize(d.name)} : ${ty} := ${val}`);
    }
  }

  private emitInstance(d: Extract<IRDecl, { tag: 'InstanceDef' }>): void {
    if (d.comment) this.emitComment(d.comment);
    const tArgs = d.typeArgs.map(t => this.typeToLean(t)).join(' ');
    this.emit(`instance : ${d.typeClass} ${tArgs} where`);
    this.ind++;
    for (const m of d.methods) {
      if (m.tag === 'FuncDef') {
        const ps = m.params.map(p => this.fmtParam(p, m.effect)).join(' ');
        const psStr = ps ? ` ${ps}` : '';
        this.emit(`${m.name}${psStr} := ${this.genExpr(m.body, m.effect)}`);
      }
    }
    this.ind--;
  }

  private emitTheorem(d: Extract<IRDecl, { tag: 'TheoremDef' }>): void {
    if (d.comment) this.emitComment(d.comment);
    this.emit(`theorem ${d.name} : ${d.statement} := by`);
    this.ind++;
    this.emit(d.proof);
    this.ind--;
  }

  private emitClass(d: Extract<IRDecl, { tag: 'ClassDecl' }>): void {
    if (d.comment) this.emitComment(d.comment);
    this.emit(`class ${d.name}${fmtTPs(d.typeParams)} where`);
    this.ind++;
    for (const m of d.methods) this.emit(`${m.name} : ${this.typeToLean(m.type)}`);
    this.ind--;
  }

  private emitNamespace(d: Extract<IRDecl, { tag: 'Namespace' }>): void {
    this.emit(`namespace ${d.name}`);
    this.emit('');
    // Check if any FuncDef in this namespace references another FuncDef by name.
    // If so, wrap all FuncDefs in a `mutual` block for forward reference support.
    const funcNames = new Set(d.decls.filter(x => x.tag === 'FuncDef').map(x => (x as any).name as string));
    const hasForwardRefs = d.decls.some(x =>
      x.tag === 'FuncDef' && funcNames.size > 1 &&
      bodyContainsAnyVarRef((x as any).body, funcNames, (x as any).name)
    );

    if (hasForwardRefs && funcNames.size > 1) {
      // Emit non-functions first, then mutual block for all functions
      for (const x of d.decls) {
        if (x.tag !== 'FuncDef') { this.emitDecl(x); this.emit(''); }
      }
      this.emit('mutual');
      this.emit('');
      for (const x of d.decls) {
        if (x.tag === 'FuncDef') { this.emitDecl(x); this.emit(''); }
      }
      this.emit('end');
    } else {
      for (const x of d.decls) { this.emitDecl(x); this.emit(''); }
    }
    this.emit(`end ${d.name}`);
  }

  // ─── Expression generation ──────────────────────────────────────────────────

  genExpr(e: IRExpr, ctx: Effect, depth = 0): string {
    // indent is ABSOLUTE — includes this.ind + depth
    const indent = '  '.repeat(this.ind + depth);
    switch (e.tag) {
      case 'LitNat':    return String(e.value);
      case 'LitInt':    return String(e.value);
      case 'LitFloat':  return Number.isInteger(e.value) ? `(${e.value} : Float)` : String(e.value);
      case 'LitString': return JSON.stringify(e.value);
      case 'LitBool':   return e.value ? 'true' : 'false';
      case 'LitUnit':   return '()';
      case 'LitNull':   return 'none';
      case 'Hole': {
        // Emit type-appropriate default instead of sorry
        return defaultForType(e.type);
      }
      case 'Var': {
        // If the name is a quoted string literal (from stdlib mapping), emit as-is
        if (e.name.startsWith('"') && e.name.endsWith('"')) return e.name;
        // Map removed AssocSet ops to Array equivalents
        if (e.name === 'AssocSet.empty') return '#[]';
        if (e.name === 'AssocSet.insert') return 'Array.push';
        if (e.name === 'AssocSet.contains') return 'Array.contains';
        return sanitize(e.name);
      }

      case 'FieldAccess': {
        // Pattern 3: Strip `.state` prefix in DO methods.
        // self.state.X → self.X; self.state.storage → default (not a real field)
        if (e.obj.tag === 'FieldAccess' && e.obj.field === 'state' &&
            e.obj.obj.tag === 'Var' && e.obj.obj.name === 'self') {
          if (e.field === 'storage') return 'default';  // Storage is handled by DO monad
          return `self.${e.field}`;
        }
        // self.storage.X → default (storage is DO runtime, not a struct field)
        if (e.obj.tag === 'FieldAccess' && e.obj.field === 'storage' &&
            e.obj.obj.tag === 'Var' && e.obj.obj.name === 'self') {
          return 'default';
        }
        // self.state.storage.X → default
        if (e.obj.tag === 'FieldAccess' && e.obj.field === 'storage') {
          const inner = e.obj.obj;
          if (inner.tag === 'FieldAccess' && inner.field === 'state' &&
              inner.obj.tag === 'Var' && inner.obj.name === 'self') {
            return 'default';
          }
        }
        const obj = this.genExpr(e.obj, ctx, depth);
        // Map JS field/method names to Lean equivalents.
        // Some names differ between String and Array — disambiguate by type.
        const isString = e.obj.type.tag === 'String';
        const fieldMap: Record<string, string> = {
          'json': 'toJson',
          'text': 'text',
          'toLowerCase': 'toLower',
          'toUpperCase': 'toUpper',
          'trim': 'trim',
          'startsWith': 'startsWith',
          'endsWith': 'endsWith',
          'pop': 'back?',
          'push': 'push',
          'reduce': 'foldl',
          'filter': 'filter',
          'map': 'map',
          'find': 'find?',
          'some': 'any',
          'every': 'all',
          'reverse': 'reverse',
          'flat': 'join',
          'flatMap': 'flatMap',
          'concat': 'append',
          'indexOf': 'indexOf?',
          'slice': 'extract',
          'split': isString ? 'splitOn' : 'split',  // String.splitOn, Array doesn't have split
          'join': isString ? 'intercalate' : 'foldl (· ++ ·) ""',  // String.intercalate, Array.join with separator
          // Type-dependent mappings:
          'length': isString ? 'length' : 'size',   // String.length, Array.size
          'size': isString ? 'length' : 'size',
          'includes': isString ? 'includes' : 'contains',  // String.includes defined in Runtime.Basic
          // toString: use standalone function, not dot notation (String.toString doesn't exist)
        };
        let mappedField = (e.field && fieldMap[e.field]) ?? e.field ?? 'default';
        if (typeof mappedField !== 'string') mappedField = String(mappedField);
        // toString/function: use standalone function, not dot notation.
        if (mappedField === 'toString' || mappedField.startsWith('function')) return `toString ${obj}`;
        // JS-specific methods with no Lean equivalent → default
        if (mappedField === 'test' || mappedField === 'getSourceFile' ||
            mappedField === 'fileExists' || mappedField === 'readFile' ||
            mappedField === 'writeFile' || mappedField === 'existsSync')
          return 'default';
        // Strip leading underscore from private field names (TS _radius → Lean radius)
        if (mappedField.startsWith('_') && mappedField.length > 1 && /[a-zA-Z]/.test(mappedField[1])) {
          mappedField = mappedField.slice(1);
        }
        // Check if the field exists on the known struct type.
        // If not (e.g. accessing inherited method on wrong subclass), use default.
        if (e.obj.type.tag === 'TypeRef') {
          const rawName = e.obj.type.name;
          const stateName = this.classToState.get(rawName) ?? rawName;
          const knownFields = this.structFields.get(stateName) ??
            this.structFields.get(rawName) ??
            this.structFields.get(rawName + 'State');
          if (knownFields) {
            if (!knownFields.some(f => f.name === mappedField)) return 'default';
          }
        }
        // String/Any types don't have arbitrary fields — emit default
        const isStringType = e.obj.type.tag === 'String' ||
          (e.obj.type.tag === 'TypeRef' && e.obj.type.name === 'String');
        const isAnyType = e.obj.type.tag === 'TypeRef' && (e.obj.type.name === 'Any' || e.obj.type.name === 'TSAny');
        const isCrossFileType = false; // Any→String mapping handles this
        const stringMethods = ['length', 'size', 'includes', 'trim',
          'toLower', 'toUpper', 'startsWith', 'endsWith', 'splitOn', 'replace'];
        if ((isStringType || isAnyType || isCrossFileType) && !stringMethods.includes(mappedField)) {
          return 'default';
        }
        return `${obj}.${mappedField}`;
      }

      case 'IndexAccess': {
        // Use Array.getD to avoid Inhabited constraint; parenthesize complex indices
        const obj = this.genExpr(e.obj, ctx, depth);
        const idx = this.genExpr(e.index, ctx, depth);
        const pObj = needsParens(e.obj) ? `(${obj})` : obj;
        const pIdx = idx.includes(' ') ? `(${idx})` : idx;
        return `${pObj}.getD ${pIdx} default`;
      }

      case 'Lambda': {
        const ps = e.params.map(p => p.name).join(' ');
        const lambdaCtx = e.effect ?? ctx;
        const bodyStr = this.genExpr(e.body, lambdaCtx, depth + 1);
        const bodyIndent = '  '.repeat(this.ind + depth + 1);
        // Wrap in `do` when body has monadic ops or sequential statements
        const needsDo = !isPure(lambdaCtx) && (
          bodyStr.includes('←') ||
          (bodyStr.includes('let ') && bodyStr.includes(':='))
        );
        // Also need `do` when body is a Sequence (multiple statements)
        const isSeq = e.body.tag === 'Sequence' || e.body.tag === 'DoBlock';
        if (needsDo || isSeq) {
          return `fun ${ps || '_'} => do\n${bodyIndent}${bodyStr}`;
        }
        // Multi-line body (if/else, match, let): put on next line for proper indentation
        if (bodyStr.includes('\n')) {
          return `fun ${ps || '_'} =>\n${bodyIndent}${bodyStr}`;
        }
        return `fun ${ps || '_'} => ${bodyStr}`;
      }

      case 'App': {
        // Fix: when calling self.methodName(args), rewrite to methodName self args.
        // Class methods are standalone defs, not struct fields.
        if (e.fn.tag === 'FieldAccess' && e.fn.obj.tag === 'Var' && e.fn.obj.name === 'self') {
          const method = e.fn.field;
          const args = e.args.map(a => this.genP(a, ctx, depth));
          return `${method} self ${args.join(' ')}`.trim();
        }
        // Rewrite method calls on known types to Lean function-call style
        if (e.fn.tag === 'FieldAccess') {
          const obj = this.genExpr(e.fn.obj, ctx, depth);
          const pObj = needsParens(e.fn.obj) ? `(${obj})` : obj;
          const method = e.fn.field;
          const isStr = e.fn.obj.type.tag === 'String' ||
            (e.fn.obj.type.tag === 'TypeRef' && e.fn.obj.type.name === 'String');
          const isArr = e.fn.obj.type.tag === 'Array';
          const args = e.args.map(a => this.genP(a, ctx, depth));
          // String methods → Lean function-style calls
          if (isStr && method === 'split')
            return args.length > 0 ? `${pObj}.splitOn ${args[0]}` : `${pObj}.splitOn ""`;
          if (isStr && method === 'replace' && args.length >= 2)
            return `${pObj}.replace ${args[0]} ${args[1]}`;
          if (isStr && method === 'startsWith' && args.length > 0)
            return `${pObj}.startsWith ${args[0]}`;
          if (isStr && method === 'endsWith' && args.length > 0)
            return `${pObj}.endsWith ${args[0]}`;
          if (isStr && method === 'includes' && args.length > 0)
            return `${pObj}.includes ${args[0]}`;
          // Array methods → function-call style (avoids invalid field projection)
          if (isArr && method === 'join' && args.length > 0)
            return `String.intercalate ${args[0]} ${pObj}`;
          if (isArr && method === 'push' && args.length > 0)
            return `Array.push ${pObj} ${args[0]}`;
          if (isArr && method === 'filter' && args.length > 0)
            return `Array.filter ${pObj} ${args[0]}`;
          if (isArr && method === 'map' && args.length > 0)
            return `Array.map ${args[0]} ${pObj}`;
          // Collection methods that need function-call style (Set.add, Map.set, etc.)
          const collectionMethods: Record<string, string> = {
            'set': 'AssocMap.insert', 'get': 'AssocMap.find?', 'has': 'AssocMap.contains',
            'delete': 'AssocMap.erase', 'add': 'Array.push',
            'keys': 'AssocMap.keys', 'values': 'AssocMap.values',
            'entries': 'AssocMap.toList', 'forEach': 'Array.forM',
          };
          if (collectionMethods[method]) {
            return args.length > 0
              ? `${collectionMethods[method]} ${pObj} ${args.join(' ')}`
              : `${collectionMethods[method]} ${pObj}`;
          }
        }
        const fn   = this.genExpr(e.fn, ctx, depth);
        // AssocMap.contains on a non-map struct type → true (field existence is static)
        if (fn === 'AssocMap.contains' && e.args.length >= 1 &&
            e.args[0].type.tag === 'TypeRef' && e.args[0].type.name !== 'Map' &&
            !e.args[0].type.name.includes('AssocMap')) {
          return 'true';
        }
        // Unresolved functions emit default
        if (fn === 'sorry' || fn === 'default') return 'default';
        // TS compiler API calls have no Lean equivalent
        if (fn.startsWith('ts.') || fn.startsWith('path.') || fn.startsWith('fs.') || fn.startsWith('process.'))
          return 'default';
        // Storage operations → pure default in monadic context
        // (Storage API needs deep DO monad integration that we can't express simply)
        if (fn.startsWith('Storage.')) return !isPure(ctx) ? 'pure default' : 'default';
        // serialize on struct literals: use toString on a string representation
        if ((fn === 'serialize' || fn === 'toString') && e.args.some(a => a.tag === 'StructLit')) {
          return `"<serialized>"`;
        }
        // Cross-file reference detection: known imported functions from other TS modules
        // that won't exist in the generated Lean file → emit default
        const CROSS_FILE_FNS = new Set([
          'parseFile', 'rewriteModule', 'generateLean', 'generateVerification',
          'transpileProject', 'writeProjectOutputs', 'irTypeToLean', 'monadString',
          'translateBinOp', 'lookupGlobal', 'lookupMethod', 'typeObjKind',
          'inferNodeEffect', 'mapType',
          'resolveSpec', 'specToLean',
        ]);
        if (e.fn.tag === 'Var' && CROSS_FILE_FNS.has(e.fn.name)) {
          return 'default /- cross-file: ' + fn + ' -/';
        }
        const args = e.args.map(a => {
          const s = this.genP(a, ctx, depth);
          // Parenthesize struct literal args to avoid Lean parser confusion
          if (s.startsWith('{') && !s.startsWith('#[')) return `(${s})`;
          return s;
        });
        return args.length === 0 ? fn : `${fn} ${args.join(' ')}`;
      }

      case 'TypeApp': {
        const fn   = this.genExpr(e.fn, ctx, depth);
        const tArgs = e.typeArgs.map(t => `(${this.typeToLean(t)})`).join(' ');
        return `${fn} ${tArgs}`;
      }

      case 'Let': {
        const v    = this.genExpr(e.value, ctx, depth);
        const body = this.genExpr(e.body, ctx, depth);
        const ann  = e.annot ? ` : ${this.typeToLean(e.annot)}` : '';
        // Self-referencing let (loop helpers): use `let rec` for recursive bindings
        const isRecursive = e.value.tag === 'Lambda' && bodyContainsVarRef(e.value.body, e.name);
        const kw = isRecursive ? 'let rec' : 'let';
        return `${kw} ${e.name}${ann} := ${v}\n${indent}${body}`;
      }

      case 'Bind': {
        const m    = this.genExpr(e.monad, ctx, depth);
        const body = this.genExpr(e.body, ctx, depth);
        // If the "monad" is actually a pure value (array literal, struct, default, number),
        // use := instead of ← (no monadic bind needed for pure expressions)
        const isPureValue = m.startsWith('#[') || m.startsWith('{') || m === 'default' ||
          m.startsWith('"') || /^\d/.test(m) || m === 'true' || m === 'false' || m === 'none';
        // When monad is `pure default`, Lean can't infer the type — add annotation
        // by matching variable name to known struct types (heuristic for storage.get)
        if (m === 'pure default' || (isPureValue && m === 'default')) {
          const typeAnn = this.inferTypeForDefault(e.name);
          if (typeAnn) return `let ${e.name} : ${typeAnn} := default\n${indent}${body}`;
        }
        const op = isPureValue ? ':=' : '←';
        return `let ${e.name} ${op} ${m}\n${indent}${body}`;
      }

      case 'IfThenElse': {
        // Detect Option null-check pattern: if x.isNone then none else expr(x)
        // Convert to: match x with | none => none | some _v => expr(_v)
        // This avoids BEq constraint issues and handles Option narrowing correctly.
        const optMatch = this.tryOptionMatch(e, ctx, depth, indent);
        if (optMatch) return optMatch;

        const cond  = this.genExpr(e.cond, ctx, depth);
        const inner = '  '.repeat(this.ind + depth + 1);
        let then_ = this.genExpr(e.then, ctx, depth + 1);
        let else_ = this.genExpr(e.else_, ctx, depth + 1);

        // In monadic context, ensure both branches return the monad type.
        if (!isPure(ctx)) {
          // () in else → pure () (Unit in the monad)
          if (else_.trim() === '()') else_ = 'pure ()';
          // Wrap pure values in `pure (...)` — but only at shallow depth
          // and only for non-monadic, non-simple values
          if (depth <= 1) {
            if (!looksMonadic(then_) && !isSimpleValue(then_)) then_ = `pure (${then_.trim()})`;
            if (!looksMonadic(else_) && !isSimpleValue(else_) && else_.trim() !== 'pure default')
              else_ = `pure (${else_.trim()})`;
          }
        }

        const thenJoin = then_.trimStart().startsWith('do') ? ' ' : `\n${inner}`;
        const elseJoin = else_.trimStart().startsWith('do') ? ' ' : `\n${inner}`;
        return `if ${cond} then${thenJoin}${then_}\n${indent}else${elseJoin}${else_}`;
      }

      case 'Match': return this.genMatch(e, ctx, depth);

      case 'Sequence': {
        if (e.stmts.length === 0) return '()';
        if (e.stmts.length === 1) return this.genExpr(e.stmts[0], ctx, depth);
        const needsDo = !isPure(ctx);  // Any non-Pure effect needs do-notation
        if (needsDo) return this.genDoSeq(e.stmts, ctx, depth);
        return e.stmts.map(s => this.genExpr(s, ctx, depth)).join(`\n${indent}`);
      }

      case 'DoBlock': return this.genDoBlock(e.stmts, ctx, depth);

      case 'StructLit': {
        // If the target type is String/Any (from TS any), can't construct with struct literal syntax
        if (e.type.tag === 'String' ||
            (e.type.tag === 'TypeRef' && e.type.name === 'Any')) {
          return 'default';
        }
        // Detect struct-update pattern: one `_base` field + other fields → `{ base with f := v }`
        const baseField = e.fields.find(f => f.name === '_base');
        const realFields = e.fields.filter(f => f.name !== '_base' && f.name !== '_spread' && !f.name.startsWith('_computed') && !f.name.startsWith('_computed'));
        if (baseField && realFields.length > 0) {
          const base = this.genExpr(baseField.value, ctx, depth);
          const updates = realFields.map(f => `${sanitize(f.name)} := ${this.genExpr(f.value, ctx, depth)}`).join(', ');
          return `{ ${base} with ${updates} }`;
        }
        // Nested struct values containing `{`: hoist to a let binding or use default
        const fieldStrs = e.fields.filter(f => f.name !== '_spread' && !f.name.startsWith('_computed')).map(f => {
          const val = this.genExpr(f.value, ctx, depth);
          const fname = sanitize(f.name);
          if (val.includes('{') && !val.startsWith('"') && !val.startsWith('#[')) {
            return `${fname} := default`;
          }
          return `${fname} := ${val}`;
        }).join(', ');
        // Empty struct literal → default (needs Inhabited instance)
        if (!fieldStrs) return 'default';
        return `{ ${fieldStrs} }`;
      }

      case 'CtorApp': {
        const args = e.args.map(a => this.genP(a, ctx, depth));
        const name = sanitize(e.ctor);
        // If ctor name looks like a plain class name (no dots, starts with uppercase),
        // and it has args, it's likely `new ClassName(...)` → use `default` as Lean
        // class constructors are just struct mk and the parser may not have the right name.
        if (args.length > 0 && /^[A-Z][a-zA-Z]*$/.test(name) && !name.includes('.')) {
          return 'default';
        }
        return args.length === 0 ? name : `${name} ${args.join(' ')}`;
      }

      case 'ArrayLit': {
        const elems = e.elems.map(x => this.genExpr(x, ctx, depth));
        // If the declared type is a Tuple, emit as tuple (a, b) not array #[a, b]
        if (e.type.tag === 'Tuple') {
          return `(${elems.join(', ')})`;
        }
        return `#[${elems.join(', ')}]`;
      }

      case 'TupleLit': return `(${e.elems.map(x => this.genExpr(x, ctx, depth)).join(', ')})`;

      case 'Pure_': return `pure ${this.genP(e.value, ctx, depth)}`;

      case 'StateGet': return 'get';
      case 'StateSet': return `set ${this.genP(e.value, ctx, depth)}`;

      case 'Throw': {
        // When error type is TSError but monad expects String, extract the message.
        // `throw (TSError.typeError "msg")` → `throw "msg"` for ExceptT String
        const errStr = this.genExpr(e.error, ctx, depth);
        if (errStr.startsWith('TSError.')) {
          // Extract the string argument from TSError.typeError "msg"
          const msgMatch = errStr.match(/"[^"]*"/);
          return `throw ${msgMatch ? msgMatch[0] : '"error"'}`;
        }
        return `throw ${needsParens(e.error) ? `(${errStr})` : errStr}`;
      }

      case 'TryCatch': {
        // tryCatch requires a MonadExcept monad. In Pure context, the monads
        // won't match — emit default for the expected type.
        if (isPure(ctx)) return 'default';
        const bodyStr = this.genExpr(e.body, ctx, depth);
        const pBody   = bodyStr.includes(' ') ? `(${bodyStr})` : bodyStr;
        const handler = this.genExpr(e.handler, ctx, depth);
        return `tryCatch ${pBody} (fun ${e.errName} => ${handler})`;
      }

      case 'Await': return this.genExpr(e.expr, ctx, depth);

      case 'Assign': {
        const val = this.genExpr(e.value, ctx, depth);
        if (e.target.tag === 'FieldAccess' && e.target.obj.tag === 'Var' && e.target.obj.name === 'self') {
          // Strip leading underscore from field name (TS _radius → Lean radius)
          let field = e.target.field;
          if (field.startsWith('_') && field.length > 1 && /[a-zA-Z]/.test(field[1]))
            field = field.slice(1);
          return `modify (fun s => { s with ${field} := ${val} })`;
        }
        if (e.target.tag === 'Var')
          return `let ${e.target.name} := ${val}`;
        return `-- assign: ${this.genExpr(e.target, ctx, depth)} := ${val}`;
      }

      case 'BinOp': return this.genBinOp(e, ctx, depth);
      case 'UnOp':  {
        const op = e.op === 'Not' ? '!' : e.op === 'Neg' ? '-' : '~~~';
        return `${op}${this.genP(e.operand, ctx, depth)}`;
      }

      case 'Cast': {
        const from = e.expr.type;
        const to   = e.targetType;
        // Cast Array → Tuple: emit as tuple literal (a, b, c) not array #[a, b, c]
        if (to.tag === 'Tuple' && e.expr.tag === 'ArrayLit') {
          return `(${e.expr.elems.map(x => this.genExpr(x, ctx, depth)).join(', ')})`;
        }
        // When casting to a branded/newtype struct, emit the constructor.
        const inner = this.genExpr(e.expr, ctx, depth);
        // Casting TO a branded struct (has a single `val` field): wrap in constructor
        if (to.tag === 'TypeRef' && from.tag === 'String') {
          return `${to.name}.mk ${inner}`;
        }
        // Casting FROM a branded struct to String: extract val field
        if (from.tag === 'TypeRef' && to.tag === 'String') {
          return `${inner}.val`;
        }
        // Casting null/none to a non-Option type: use default
        if ((inner === 'none' || inner === 'default') && to.tag !== 'Option') {
          return 'default';
        }
        return inner;
      }
      case 'IsType': {
        // instanceof / type guard — no runtime type inspection in Lean 4, so emit sorry
        // as a proof obligation: the caller must supply evidence of the type relationship.
        const typeName = e.testType.tag === 'TypeRef' ? e.testType.name
                       : e.testType.tag === 'Structure' ? e.testType.name
                       : this.typeToLean(e.testType);
        const expr = this.genExpr(e.expr, ctx, depth);
        return `(sorry : Bool) /- ${expr} matches ${typeName} -/`;
      }

      case 'Return': {
        const val = this.genExpr(e.value, ctx, depth);
        return !isPure(ctx) ? `pure ${needsParens(e.value) ? `(${val})` : val}` : val;
      }

      case 'Panic': return `panic! ${JSON.stringify(e.msg)}`;

      // New in v3:
      case 'StructUpdate': {
        const base = this.genExpr(e.base, ctx, depth);
        const realFields = e.fields.filter(f => !f.name.startsWith('_computed'));
        if (realFields.length === 0) return base;  // pure spread
        const updates = realFields.map(f => {
          const val = this.genExpr(f.value, ctx, depth);
          if (val.includes('{') && !val.startsWith('"') && !val.startsWith('#[')) {
             return `${f.name} := default`;
          }
          return `${f.name} := ${val}`;
        }).join(', ');
        return `{ ${base} with ${updates} }`;
      }

      case 'TypeNarrow': {
        // Type narrowing becomes a cast or match — emit the inner expression
        return this.genExpr(e.expr, ctx, depth);
      }

      case 'YieldExpr': {
        const val = e.value ? this.genExpr(e.value, ctx, depth) : '()';
        return `yield ${val}`;
      }

      case 'OptChain': {
        return this.genExpr(e.expr, ctx, depth);
      }

      case 'MultiLet': {
        const ind = '  '.repeat(this.ind + depth);
        let result = this.genExpr(e.body, ctx, depth);
        for (const b of [...e.bindings].reverse()) {
          const val = this.genExpr(b.value, ctx, depth);
          const ann = ` : ${this.typeToLean(b.type)}`;
          result = `let ${b.name}${ann} := ${val}\n${ind}${result}`;
        }
        return result;
      }

      case 'Labeled': return this.genExpr(e.body, ctx, depth);
      case 'Break':   return '-- break';
      case 'Continue':return '-- continue';

      default: return 'default';
    }
  }

  private genP(e: IRExpr, ctx: Effect, depth: number): string {
    const s = this.genExpr(e, ctx, depth);
    return needsParens(e) ? `(${s})` : s;
  }

  private genMatch(e: Extract<IRExpr, { tag: 'Match' }>, ctx: Effect, depth: number): string {
    const scrutinee = this.genExpr(e.scrutinee, ctx, depth);
    const inner     = '  '.repeat(this.ind + depth + 1);
    const cases     = e.cases.map(c => {
      const pat   = this.genPat(c.pattern);
      const guard = c.guard ? ` if ${this.genExpr(c.guard, ctx, depth)}` : '';
      const body  = this.genExpr(c.body, ctx, depth + 1);
      return `${inner}| ${pat}${guard} => ${body}`;
    });
    return `match ${scrutinee} with\n${cases.join('\n')}`;
  }

  private genPat(p: IRPattern): string {
    switch (p.tag) {
      case 'PVar':    return sanitize(p.name);
      case 'PWild':   return '_';
      case 'PLit':    return typeof p.value === 'string' ? JSON.stringify(p.value) : String(p.value);
      case 'PString': return JSON.stringify(p.value);
      case 'PNone':   return '.none';
      case 'PSome':   return `.some ${this.genPat(p.inner)}`;
      case 'PCtor': {
        const localName = p.ctor.includes('.') ? p.ctor.split('.').pop()! : p.ctor;
        const args = p.args.map(a => this.genPat(a));
        return args.length === 0 ? `.${localName}` : `.${localName} ${args.join(' ')}`;
      }
      case 'PTuple':  return `(${p.elems.map(x => this.genPat(x)).join(', ')})`;
      case 'PStruct': return `{ ${p.fields.map(f => `${f.name} := ${this.genPat(f.pattern)}`).join(', ')} }`;
      case 'POr':     return p.pats.map(x => this.genPat(x)).join(' | ');
      case 'PAs':     return `${this.genPat(p.pattern)} as ${p.name}`;
      default:        return '_';
    }
  }

  private genDoBlock(stmts: DoStmt[], ctx: Effect, depth: number): string {
    const ind = '  '.repeat(this.ind + depth + 1);
    const lines = ['do'];
    for (const s of stmts) {
      switch (s.tag) {
        case 'DoBind':   lines.push(`${ind}let ${s.name} ← ${this.genExpr(s.expr, ctx, depth + 1)}`); break;
        case 'DoLet':    lines.push(`${ind}let ${s.name} := ${this.genExpr(s.value, ctx, depth + 1)}`); break;
        case 'DoExpr':   lines.push(`${ind}${this.genExpr(s.expr, ctx, depth + 1)}`); break;
        case 'DoReturn': lines.push(`${ind}return ${this.genExpr(s.value, ctx, depth + 1)}`); break;
      }
    }
    return lines.join('\n');
  }

  private genDoSeq(stmts: IRExpr[], ctx: Effect, depth: number): string {
    const ind = '  '.repeat(this.ind + depth + 1);
    const lines = ['do'];

    // Pre-process: chain sequential IfThenElse(cond, body, LitUnit) nodes
    // into a single if-else-if chain. This is the key DO pattern:
    //   if cond1 then return resp1
    //   if cond2 then return resp2
    //   return defaultResp
    // becomes: if cond1 then ... else if cond2 then ... else ...
    const processed = this.chainSequentialIfs(stmts);

    for (const s of processed) {
      if (s.tag === 'Bind') {
        const mStr = this.genExpr(s.monad, ctx, depth + 1);
        const isPureVal = mStr.startsWith('#[') || mStr.startsWith('{') || mStr === 'default' ||
          mStr.startsWith('"') || /^\d/.test(mStr) || mStr === 'true' || mStr === 'false' || mStr === 'none';
        const op = isPureVal ? ':=' : '←';
        lines.push(`${ind}let ${s.name} ${op} ${mStr}`);
      } else if (s.tag === 'Let') {
        const isRec = s.value.tag === 'Lambda' && bodyContainsVarRef(s.value.body, s.name);
        const kw = isRec ? 'let rec' : 'let';
        lines.push(`${ind}${kw} ${s.name} := ${this.genExpr(s.value, ctx, depth + 1)}`);
      } else if (s.tag === 'Return') {
        lines.push(`${ind}return ${this.genExpr(s.value, ctx, depth + 1)}`);
      } else {
        const expr = this.genExpr(s, ctx, depth + 1);
        // In do blocks, non-last expressions that return non-Unit (like Array.push)
        // need `let _ :=` to discard the return value
        if (expr.includes('.push ') || expr.includes('.push(')) {
          lines.push(`${ind}let _ := ${expr}`);
        } else {
          lines.push(`${ind}${expr}`);
        }
      }
    }
    return lines.join('\n');
  }

  /** Chain sequential if-then-unit statements into a single if-else-if chain.
   *  Input:  [IfThenElse(c1, b1, ()), IfThenElse(c2, b2, ()), returnDefault]
   *  Output: [IfThenElse(c1, b1, IfThenElse(c2, b2, returnDefault))] */
  private chainSequentialIfs(stmts: IRExpr[]): IRExpr[] {
    // Find runs of IfThenElse where else_ is LitUnit or pure ()
    const result: IRExpr[] = [];
    let i = 0;
    while (i < stmts.length) {
      const s = stmts[i];
      if (s.tag === 'IfThenElse' && isEmptyElse(s.else_) && i + 1 < stmts.length) {
        // Start of an if-chain. Collect all consecutive if-then-() patterns.
        const chain: Array<{ cond: IRExpr; then: IRExpr }> = [{ cond: s.cond, then: s.then }];
        i++;
        while (i < stmts.length && stmts[i].tag === 'IfThenElse' && isEmptyElse((stmts[i] as any).else_)) {
          const next = stmts[i] as Extract<IRExpr, { tag: 'IfThenElse' }>;
          chain.push({ cond: next.cond, then: next.then });
          i++;
        }
        // The remaining statement (if any) becomes the final else
        const finalElse: IRExpr = i < stmts.length ? stmts[i] : { tag: 'LitUnit', type: s.type, effect: s.effect };
        if (i < stmts.length) i++;  // consume the final else statement

        // Build the chained if-else-if from right to left
        let chained: IRExpr = finalElse;
        for (let j = chain.length - 1; j >= 0; j--) {
          chained = {
            tag: 'IfThenElse',
            cond: chain[j].cond,
            then: chain[j].then,
            else_: chained,
            type: s.type,
            effect: s.effect,
          };
        }
        result.push(chained);
      } else {
        result.push(s);
        i++;
      }
    }
    return result;
  }

  private genBinOp(e: Extract<IRExpr, { tag: 'BinOp' }>, ctx: Effect, depth: number): string {
    if (e.op === 'NullCoalesce') {
      const l = this.genP(e.left, ctx, depth);
      const r = this.genP(e.right, ctx, depth);
      // When left is default/pure default (from Storage stub), use the fallback directly
      if (l === 'default' || l === '(pure default)' || l === 'pure default') return r;
      return `Option.getD ${l} ${r}`;
    }
    // Equality with none/null: use Option.isNone/isSome instead of ==
    // This avoids requiring BEq instances on generic Option types.
    if (e.op === 'Eq' && (e.right.tag === 'LitNull' || (e.right.tag === 'Var' && e.right.name === 'none'))) {
      const l = this.genP(e.left, ctx, depth);
      return `${l}.isNone`;
    }
    if (e.op === 'Ne' && (e.right.tag === 'LitNull' || (e.right.tag === 'Var' && e.right.name === 'none'))) {
      const l = this.genP(e.left, ctx, depth);
      return `${l}.isSome`;
    }
    // Try to emit s!"..." interpolation for Concat chains (template literals)
    if (e.op === 'Concat' || (e.op === 'Add' && e.left.type.tag === 'String')) {
      const interp = this.trySInterp(e, ctx, depth);
      if (interp) return interp;
    }
    const l = this.genP(e.left, ctx, depth);
    const r = this.genP(e.right, ctx, depth);
    // Comparison on Any type: no Ord/OfNat instance → default
    const isAnyL = (e.left.type.tag === 'TypeRef' && (e.left.type.name === 'Any' || e.left.type.name === 'TSAny')) ||
      (e.left.tag === 'TypeNarrow' && e.left.expr.type.tag === 'TypeRef' && (e.left.expr.type.name === 'Any' || e.left.expr.type.name === 'TSAny'));
    const isAnyR = (e.right.type.tag === 'TypeRef' && (e.right.type.name === 'Any' || e.right.type.name === 'TSAny')) ||
      (e.right.tag === 'TypeNarrow' && e.right.expr.type.tag === 'TypeRef' && (e.right.expr.type.name === 'Any' || e.right.expr.type.name === 'TSAny'));
    if ((e.op === 'Lt' || e.op === 'Le' || e.op === 'Gt' || e.op === 'Ge') && (isAnyL || isAnyR)) {
      return '(sorry : Bool)';
    }
    const op = translateBinOp(e.op, e.left.type);
    return `${l} ${op} ${r}`;
  }

  // Try to fold a Concat/Add-string chain into Lean 4 s!"..." interpolation.
  // Returns null if the chain is not a simple template (e.g. contains function calls).
  private trySInterp(e: IRExpr, ctx: Effect, depth: number): string | null {
    const parts = flattenConcat(e);
    if (!parts) return null;
    // Require both: at least one interpolated expression AND at least one non-empty string literal.
    // This ensures s! only fires for real template literals ("`Hello, ${name}!`"),
    // not bare string concatenations (`a ++ b`) which are more readable as `++`.
    const hasInterp  = parts.some(p => p.tag !== 'LitString');
    const hasLiteral = parts.some(p => p.tag === 'LitString' && p.value !== '');
    if (!hasInterp || !hasLiteral) return null;
    // Only safe for simple expressions (vars, field accesses, literals)
    if (!parts.every(isSafeInterp)) return null;
    // Can't use s!"..." if any literal part contains { } or " — Lean has no escape for these
    const hasUnsafeChars = parts.some(p =>
      p.tag === 'LitString' && (/[{}"\\]/.test((p as any).value))
    );
    if (hasUnsafeChars) return null;
    const inner = parts.map(p => {
      if (p.tag === 'LitString') return (p as any).value;
      return `{${this.genExpr(p, ctx, depth)}}`;
    }).join('');
    return `s!"${inner}"`;
  }

  /**
   * Detect `if opt.isNone then none else f(opt)` patterns and convert them to
   * `match opt with | none => none | some _v => f _v`.
   * This is necessary because Option T has no BEq instance for generic T,
   * and the `else` branch needs the unwrapped value (T, not Option T).
   */
  private tryOptionMatch(
    e: Extract<IRExpr, { tag: 'IfThenElse' }>,
    ctx: Effect, depth: number, indent: string
  ): string | null {
    // Pattern: cond is `BinOp Eq left LitNull` or the genExpr would produce `x.isNone`
    const cond = e.cond;
    let optVar: string | null = null;
    let optVarExpr: IRExpr | null = null;

    // Check: cond is BinOp(Eq, someVar, LitNull) or BinOp(Eq, someVar, Var("none"))
    if (cond.tag === 'BinOp' && cond.op === 'Eq' &&
        (cond.right.tag === 'LitNull' || (cond.right.tag === 'Var' && cond.right.name === 'none'))) {
      if (cond.left.tag === 'Var') {
        optVar = cond.left.name;
        optVarExpr = cond.left;
      }
    }
    // Also check if cond is the inverted form (Ne → swap then/else)
    if (!optVar && cond.tag === 'BinOp' && cond.op === 'Ne' &&
        (cond.right.tag === 'LitNull' || (cond.right.tag === 'Var' && cond.right.name === 'none'))) {
      if (cond.left.tag === 'Var') {
        optVar = cond.left.name;
        optVarExpr = cond.left;
        // Swap branches for Ne (if x != none then expr else none → match x with some → expr | none → none)
        const inner = '  '.repeat(this.ind + depth + 1);
        const someBody = this.genExprWithVarSubst(e.then, ctx, depth + 1, optVar, '_v');
        const noneBody = this.genExpr(e.else_, ctx, depth + 1);
        return `match ${optVar} with\n${inner}| none => ${noneBody}\n${inner}| some _v => ${someBody}`;
      }
    }

    if (!optVar) return null;

    // then branch should be `none` or `LitNull` (the null/undefined return)
    const thenIsNone = e.then.tag === 'LitNull' || e.then.tag === 'LitUnit' ||
      (e.then.tag === 'Var' && e.then.name === 'none') ||
      (e.then.tag === 'Return' && (e.then.value.tag === 'LitNull' || (e.then.value.tag === 'Var' && e.then.value.name === 'none')));

    if (!thenIsNone) return null;

    // else branch uses optVar — substitute with the unwrapped `_v`
    const inner = '  '.repeat(this.ind + depth + 1);
    const someBody = this.genExprWithVarSubst(e.else_, ctx, depth + 1, optVar, '_v');
    return `match ${optVar} with\n${inner}| none => none\n${inner}| some _v => ${someBody}`;
  }

  /** Generate an expression but substitute one variable name with another. */
  private genExprWithVarSubst(e: IRExpr, ctx: Effect, depth: number, from: string, to: string): string {
    // Simple textual substitution: generate normally then replace the variable.
    // This works for simple cases where the variable appears as a standalone token.
    const result = this.genExpr(e, ctx, depth);
    // Use word-boundary replacement to avoid partial matches
    return result.replace(new RegExp(`\\b${from}\\b`, 'g'), to);
  }

  private emitComment(c: string): void {
    for (const line of c.split('\n')) {
      const stripped = line.replace(/^\/\*+\s*/, '').replace(/\*+\/$/, '').replace(/^\s*\*\s?/, '').trim();
      if (!stripped) continue;  // Skip blank lines from JSDoc formatting
      if (stripped.startsWith('@')) continue;  // Skip JSDoc tags (@param, @returns, etc.)
      this.emit(`-- ${stripped}`);
    }
  }

  /** Emit a line (or multi-line string). All lines get the current indent prefix. */
  /** Emit code. Multi-line strings: first line gets this.ind prefix,
   *  continuation lines already have absolute indentation from genExpr. */
  private emit(s: string): void {
    const prefix = '  '.repeat(this.ind);
    const lines = s.split('\n');
    this.lines.push(prefix + lines[0]);
    for (let i = 1; i < lines.length; i++) {
      this.lines.push(lines[i]);
    }
  }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

const LEAN_KWS = new Set([
  'def','fun','let','in','if','then','else','match','with','do','return','where',
  'have','show','from','by','class','instance','structure','inductive','namespace',
  'end','open','import','theorem','lemma','example','variable','universe','abbrev',
  'opaque','partial','mutual','private','protected','section','attribute','and','or',
  'not','true','false','Type','Prop',
  'for','while','repeat','at','try','catch','throw','macro','syntax','tactic',
  'set_option','derive','deriving','extends','override',
]);

function sanitize(name: string): string {
  if (LEAN_KWS.has(name)) return `«${name}»`;
  // Lean 4 allows ? at the end of identifiers (for Option-returning functions like Array.get?)
  // Also allow ! for panic functions. Replace other special chars with _.
  return name.replace(/[^a-zA-Z0-9_.!?']/g, '_');
}

/** Format type parameters as implicit ({T : Type}) — used for function definitions. */
function fmtTPs(params: string[]): string {
  return params.length === 0 ? '' : ` {${params.map(p => `${p} : Type`).join('} {')}}`;
}

/** Format type parameters as explicit ((T : Type)) — used for structures and inductives.
 *  Explicit params let Lean infer type args at use sites without `(T := ...)` annotations. */
function fmtExplicitTPs(params: string[]): string {
  return params.length === 0 ? '' : ` ${params.map(p => `(${p} : Type)`).join(' ')}`;
}

/** Determine if an expression body needs `do` wrapping when inside an effectful function.
 *  Returns true when the body contains monadic operations (Bind/Await/Sequence with effects)
 *  that aren't already inside a DoBlock. */
function needsDoWrapping(body: IRExpr): boolean {
  switch (body.tag) {
    case 'DoBlock':  return false;  // Already has do
    case 'Bind':     return true;   // let x ← ... needs do
    case 'Await':    return true;   // await needs do
    case 'Sequence': return true;   // multi-step effectful sequence needs do
    case 'Let':      return needsDoWrapping(body.body);
    case 'IfThenElse':
      return needsDoWrapping(body.then) || needsDoWrapping(body.else_);
    default:         return false;
  }
}

/** Emit a type-appropriate default value instead of sorry.
 *  Uses `default` (requires Inhabited instance) for most types,
 *  but emits concrete literals for common types. */
function defaultForType(t: IRType): string {
  switch (t.tag) {
    case 'Nat':     return '0';
    case 'Int':     return '0';
    case 'Float':   return '(0 : Float)';
    case 'String':  return '""';
    case 'Bool':    return 'false';
    case 'Unit':    return '()';
    case 'Array':   return '#[]';
    case 'Option':  return 'none';
    case 'Tuple':   return t.elems.length === 0 ? '()' : `(${t.elems.map(defaultForType).join(', ')})`;
    default:        return 'default';  // Lean's `default` requires Inhabited instance
  }
}

/** Check if a generated string looks like a monadic expression (already in the monad).
 *  Pure values like `mkResponse ...` need `pure (...)` wrapping in do blocks. */
/** Check if a string is a simple value (number, string literal, boolean, default).
 *  These should NOT get `pure` wrapping even in monadic context. */
function isSimpleValue(s: string): boolean {
  const t = s.trim();
  return /^\d+$/.test(t) || /^\(?\d+\s*:\s*Float\)?$/.test(t) ||
         t.startsWith('"') || t === 'true' || t === 'false' ||
         t === 'default' || t === 'none' || t === '#[]';
}

/** Collect all FIELD names (not method calls) accessed on a given variable.
 *  self.field → collected; self.method() → NOT collected (it's a method call). */
function collectFieldAccesses(expr: IRExpr, varName: string): Map<string, IRType> {
  const fields = new Map<string, IRType>();
  const methodCalls = new Set<string>();

  function walk(e: IRExpr): void {
    if (!e || typeof e !== 'object') return;
    // Detect method calls: App(FieldAccess(self, method), args) → skip the field
    if (e.tag === 'App' && e.fn.tag === 'FieldAccess' &&
        e.fn.obj.tag === 'Var' && e.fn.obj.name === varName) {
      methodCalls.add(e.fn.field);
    }
    // Collect field accesses
    if (e.tag === 'FieldAccess' && e.obj.tag === 'Var' && e.obj.name === varName) {
      fields.set(e.field, e.type);
    }
    for (const v of Object.values(e)) {
      if (v && typeof v === 'object') {
        if (Array.isArray(v)) {
          for (const item of v) {
            if (item && typeof item === 'object' && 'tag' in item) walk(item as IRExpr);
            if (item && typeof item === 'object') {
              for (const sub of Object.values(item)) {
                if (sub && typeof sub === 'object' && 'tag' in (sub as any)) walk(sub as IRExpr);
              }
            }
          }
        } else if ('tag' in v) walk(v as IRExpr);
      }
    }
  }
  walk(expr);
  // Remove method calls from field set
  for (const m of methodCalls) fields.delete(m);
  return fields;
}

/** Check if an else branch is "empty" (LitUnit, pure (), or trivial). */
function isEmptyElse(e: IRExpr): boolean {
  if (e.tag === 'LitUnit') return true;
  if (e.tag === 'Pure_' && e.value.tag === 'LitUnit') return true;
  return false;
}

function looksMonadic(s: string): boolean {
  const t = s.trimStart();
  return t.startsWith('do') || t.startsWith('pure ') || t.startsWith('return ') ||
         t.startsWith('let ') || t.startsWith('modify ') || t.startsWith('throw ') ||
         t.startsWith('tryCatch ') || t.startsWith('if ') || t.startsWith('match ') ||
         t === '()' || t === 'default' ||
         t.startsWith('pure default') || t.startsWith('pure ()');
}

function needsParens(e: IRExpr): boolean {
  return e.tag === 'App' || e.tag === 'BinOp' || e.tag === 'UnOp' ||
         e.tag === 'IfThenElse' || e.tag === 'Lambda' || e.tag === 'Let' ||
         e.tag === 'Bind' || e.tag === 'TryCatch' || e.tag === 'Match' ||
         e.tag === 'LitFloat';
}

// ─── State effect recovery ────────────────────────────────────────────────────
// When the parser emits stateEffect(TyUnit) for assignments, we recover the real
// state type from the `self` parameter at codegen time.

function fixStateEffect(eff: Effect, params: IRParam[]): Effect {
  const selfParam = params.find(p => p.name === 'self');
  if (!selfParam) return eff;

  if (eff.tag === 'State' && eff.stateType.tag === 'Unit') {
    return { tag: 'State', stateType: selfParam.type };
  }
  if (eff.tag === 'Combined') {
    return {
      tag: 'Combined',
      effects: eff.effects.map(e =>
        e.tag === 'State' && e.stateType.tag === 'Unit'
          ? { tag: 'State' as const, stateType: selfParam.type }
          : e
      ),
    };
  }
  return eff;
}

// ─── Partial def detection ────────────────────────────────────────────────────

/** Check if an expression references ANY name from a set (excluding selfName).
 *  Recurses deeply into ALL expression types. */
function bodyContainsAnyVarRef(expr: IRExpr, names: Set<string>, selfName: string): boolean {
  function check(e: IRExpr): boolean {
    if (!e || typeof e !== 'object') return false;
    if (e.tag === 'Var' && names.has(e.name) && e.name !== selfName) return true;
    if (e.tag === 'FieldAccess' && e.obj.tag === 'Var' && e.obj.name === 'self') {
      // Check if field matches any name (plain or ClassName.method)
      if (names.has(e.field)) return true;
      for (const n of names) if (n.endsWith(`.${e.field}`)) return true;
    }
    // Recurse into ALL child expression fields
    for (const [_k, v] of Object.entries(e)) {
      if (v && typeof v === 'object') {
        if (Array.isArray(v)) {
          for (const item of v) {
            if (item && typeof item === 'object' && 'tag' in item && check(item as IRExpr)) return true;
            // Also check array of {body, value, expr, etc.} objects (IRCase, DoStmt)
            if (item && typeof item === 'object') {
              for (const sub of Object.values(item)) {
                if (sub && typeof sub === 'object' && 'tag' in (sub as any) && check(sub as IRExpr)) return true;
              }
            }
          }
        } else if ('tag' in v) {
          if (check(v as IRExpr)) return true;
        }
      }
    }
    return false;
  }
  return check(expr);
}

function bodyContainsVarRef(expr: IRExpr, name: string): boolean {
  function check(e: IRExpr): boolean {
    if (e.tag === 'Var' && e.name === name) return true;
    switch (e.tag) {
      case 'App':        return check(e.fn) || e.args.some(check);
      case 'Lambda':     return check(e.body);
      case 'Let':        return check(e.value) || check(e.body);
      case 'Bind':       return check(e.monad) || check(e.body);
      case 'IfThenElse': return check(e.cond)  || check(e.then) || check(e.else_);
      case 'Match':      return check(e.scrutinee) || e.cases.some(c => check(c.body));
      case 'Sequence':   return e.stmts.some(check);
      case 'BinOp':      return check(e.left) || check(e.right);
      case 'UnOp':       return check(e.operand);
      case 'FieldAccess':return check(e.obj);
      case 'IndexAccess':return check(e.obj) || check(e.index);
      case 'Assign':     return check(e.target) || check(e.value);
      case 'TryCatch':   return check(e.body) || check(e.handler);
      case 'Throw':      return check(e.error);
      case 'Return':     return check(e.value);
      case 'Await':      return check(e.expr);
      case 'Cast':       return check(e.expr);
      case 'CtorApp':    return e.args.some(check);
      case 'ArrayLit':   return e.elems.some(check);
      case 'TupleLit':   return e.elems.some(check);
      case 'StructLit':  return e.fields.some(f => check(f.value));
      case 'DoBlock':    return e.stmts.some(s => {
        switch (s.tag) {
          case 'DoBind':   return check(s.expr);
          case 'DoLet':    return check(s.value);
          case 'DoExpr':   return check(s.expr);
          case 'DoReturn': return check(s.value);
        }
      });
      default: return false;
    }
  }
  return check(expr);
}

// ─── Mutual recursion detection ───────────────────────────────────────────────

function groupMutual(decls: IRDecl[]): IRDecl[][] {
  // Build adjacency: which FuncDefs reference which others
  const funcNames = new Set<string>(
    decls.filter((d): d is Extract<IRDecl, { tag: 'FuncDef' }> => d.tag === 'FuncDef').map(d => d.name)
  );
  const groups: IRDecl[][] = [];
  const visited = new Set<number>();

  for (let i = 0; i < decls.length; i++) {
    if (visited.has(i)) continue;
    const d = decls[i];
    if (d.tag !== 'FuncDef') { groups.push([d]); visited.add(i); continue; }

    // Find all FuncDefs this one mutually references (within the same namespace)
    const mutualGroup = [d];
    visited.add(i);
    for (let j = i + 1; j < decls.length; j++) {
      if (visited.has(j)) continue;
      const d2 = decls[j];
      if (d2.tag !== 'FuncDef') continue;
      // Mutual if d references d2.name AND d2 references d.name
      const fd  = d  as Extract<IRDecl, { tag: 'FuncDef' }>;
      const fd2 = d2 as Extract<IRDecl, { tag: 'FuncDef' }>;
      if (bodyContainsVarRef(fd.body, fd2.name) &&
          bodyContainsVarRef(fd2.body, fd.name)) {
        mutualGroup.push(d2);
        visited.add(j);
      }
    }
    groups.push(mutualGroup);
  }
  return groups;
}

// ─── s!"..." interpolation helpers ───────────────────────────────────────────

function flattenConcat(e: IRExpr): IRExpr[] | null {
  if (e.tag === 'LitString') return [e];
  // Non-string-concat binary: stop
  if (e.tag === 'BinOp' && (e.op === 'Concat' || (e.op === 'Add' && e.left.type.tag === 'String'))) {
    const left  = flattenConcat(e.left);
    const right = flattenConcat(e.right);
    if (!left || !right) return null;
    return [...left, ...right];
  }
  // Safe interpolation leaf nodes: Var, FieldAccess, LitNat, LitBool, Cast
  if (isSafeInterp(e)) return [e];
  return null;
}

function isSafeInterp(e: IRExpr): boolean {
  switch (e.tag) {
    case 'LitString':
    case 'LitNat':
    case 'LitFloat':
    case 'LitBool':
    case 'Var':
      return true;
    case 'FieldAccess':
      return isSafeInterp(e.obj);
    case 'Cast':
      return isSafeInterp(e.expr);
    default:
      return false;
  }
}
