/**
 * Tests for V2 codegen pipeline (IR → LeanAST → Text).
 *
 * Strategy: run V2 on fixture files, verify the output:
 * 1. Contains the expected Lean constructs
 * 2. Produces structurally valid Lean (no post-processing needed)
 * 3. Progressively converges with V1 output
 */

import { describe, it, expect } from 'vitest';
import * as path from 'path';
import { generateLeanV2 } from '../src/codegen/v2.js';
import { generateLean } from '../src/codegen/index.js';
import { parseFile } from '../src/parser/index.js';
import { rewriteModule } from '../src/rewrite/index.js';
import type { IRModule } from '../src/ir/types.js';

const FIX = path.join(process.cwd(), 'tests/fixtures');

function parseToIR(rel: string): IRModule {
  const mod = parseFile({ fileName: path.join(FIX, rel) });
  return rewriteModule(mod);
}

describe('V2 codegen: basic outputs', () => {
  it('generates valid Lean for hello.ts', () => {
    const mod = parseToIR('basic/hello.ts');
    const output = generateLeanV2(mod);

    // Must contain key structural elements
    expect(output).toContain('-- Auto-generated by ts-lean-transpiler');
    expect(output).toContain('import TSLean.Runtime.Basic');
    expect(output).toContain('import TSLean.Runtime.Coercions');
    expect(output).toContain('open TSLean');
    expect(output).toContain('namespace TSLean.Generated.Hello');
    expect(output).toContain('end TSLean.Generated.Hello');

    // Must contain function definitions
    expect(output).toContain('def greet');
    expect(output).toContain('(name : String)');
    expect(output).toContain(': String :=');
    expect(output).toContain('s!"Hello, {name}!"');

    expect(output).toContain('def add');
    expect(output).toContain('(a : Float)');

    expect(output).toContain('partial def factorial');
    expect(output).toMatch(/if n <= (\(0 : Float\)|0) then/);

    expect(output).toContain('def PI : Float :=');
    expect(output).toContain('3.14159');
  });

  it('generates valid Lean for discriminated-unions.ts', () => {
    const mod = parseToIR('generics/discriminated-unions.ts');
    const output = generateLeanV2(mod);

    // Inductives
    expect(output).toContain('inductive Shape where');
    expect(output).toContain('| Circle (radius : Float)');
    expect(output).toContain('| Rectangle (width : Float) (height : Float)');
    expect(output).toContain('deriving Repr, BEq, Inhabited');

    expect(output).toContain('inductive Color where');
    expect(output).toContain('| Red');

    // Generic inductive
    expect(output).toContain('inductive Tree (T : Type) where');
    expect(output).toContain('| Leaf (value : T)');

    // Pattern matching
    expect(output).toContain('match s with');
    expect(output).toContain('.Circle radius');
    expect(output).toContain('.Rectangle width height');
  });

  it('generates valid Lean for interfaces.ts', () => {
    const mod = parseToIR('basic/interfaces.ts');
    const output = generateLeanV2(mod);

    // Structures from interfaces
    expect(output).toContain('structure');
    expect(output).toContain('where');
    expect(output).toContain('mk ::');
  });
});

