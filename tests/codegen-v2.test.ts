describe('V2 vs V1 comparison', () => {
  it('V2 output is structurally similar to V1 for hello.ts', () => {
    const mod = parseToIR('basic/hello.ts');
    const v1 = generateLean(mod);
    const v2 = generateLeanV2(mod);

    // Both should define the same functions
    const v1Funcs = (v1.match(/\bdef \w+/g) ?? []).sort();
    const v2Funcs = (v2.match(/\bdef \w+/g) ?? []).sort();
    expect(v2Funcs).toEqual(v1Funcs);

    // Both should have the same namespace
    expect(v2).toContain('namespace TSLean.Generated.Hello');
  });

  it('V2 output is structurally similar to V1 for discriminated-unions.ts', () => {
    const mod = parseToIR('generics/discriminated-unions.ts');
    const v1 = generateLean(mod);
    const v2 = generateLeanV2(mod);

    // Both should define the same inductives
    const v1Inductives = (v1.match(/\binductive \w+/g) ?? []).sort();
    const v2Inductives = (v2.match(/\binductive \w+/g) ?? []).sort();
    expect(v2Inductives).toEqual(v1Inductives);

    // Both should define the same functions
    const v1Funcs = (v1.match(/\b(?:partial )?def \w+/g) ?? []).sort();
    const v2Funcs = (v2.match(/\b(?:partial )?def \w+/g) ?? []).sort();
    expect(v2Funcs).toEqual(v1Funcs);
  });
});
