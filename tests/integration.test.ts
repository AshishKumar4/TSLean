// Integration tests: full pipeline parse → rewrite → codegen.

import { describe, it, expect, beforeAll } from 'vitest';
import * as path from 'path';
import { parseFile } from '../src/parser/index.js';
import { rewriteModule } from '../src/rewrite/index.js';
import { generateLean } from '../src/codegen/index.js';

const FIX = path.join(process.cwd(), 'tests/fixtures');

function pipeline(rel: string): string {
  const mod = parseFile({ fileName: path.join(FIX, rel) });
  return generateLean(rewriteModule(mod));
}

// ─── basic/hello.ts ───────────────────────────────────────────────────────────

describe('Integration: basic/hello.ts', () => {
  let code: string;
  beforeAll(() => { code = pipeline('basic/hello.ts'); });

  it('non-empty output',                 () => expect(code.length).toBeGreaterThan(0));
  it('def greet',                        () => expect(code).toContain('def greet'));
  it('def add',                          () => expect(code).toContain('def add'));
  it('def isPositive',                   () => expect(code).toContain('def isPositive'));
  it('def factorial',                    () => expect(code).toContain('def factorial'));
  it('auto-generated header',            () => expect(code).toContain('Auto-generated by ts-lean-transpiler'));
  it('open TSLean',                      () => expect(code).toContain('open TSLean'));
  it('no raw TS function keyword',       () => expect(code).not.toContain('\nfunction '));
  it('no const keyword',                 () => expect(code).not.toContain('\nconst '));
  it('no === operator',                  () => expect(code).not.toContain('==='));
  it('factorial has if',                 () => { const idx = code.indexOf('def factorial'); const fn = code.slice(idx, code.indexOf('\ndef ', idx + 1)); expect(fn).toContain('if'); });
  it('PI as Float constant',             () => expect(code).toMatch(/def PI.*Float/));
  it('greet uses string concat (++ or s!"...")',    () => { const idx = code.indexOf('def greet'); const fn = code.slice(idx, code.indexOf('\ndef ', idx + 1) || undefined); expect(fn).toMatch(/\+\+|s!"[^"]*"/); });
});

