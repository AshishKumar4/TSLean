// Tests for the Lean 4 code generator.

import { describe, it, expect } from 'vitest';
import { generateLean } from '../src/codegen/index.js';
import {
  IRModule, tp, IRDecl, IRExpr,
  TyString, TyFloat, TyBool, TyUnit, TyNat, TyArray, TyOption, TyRef, TyMap, TySet, TyTuple,
  Pure, IO, Async, stateEffect, exceptEffect, combineEffects,
  litStr, litNat, litBool, litUnit, varExpr, holeExpr,
} from '../src/ir/types.js';

function mod(decls: IRDecl[]): IRModule {
  return { name: 'TSLean.Test', imports: [{ module: 'TSLean.Runtime.Basic' }], decls, comments: [], sourceFile: 'test.ts' };
}

// ─── Header ───────────────────────────────────────────────────────────────────

describe('generateLean – header', () => {
  it('emits auto-generated comment', () => expect(generateLean(mod([]))).toContain('Auto-generated by ts-lean-transpiler'));
  it('emits open TSLean',            () => expect(generateLean(mod([]))).toContain('open TSLean'));
  it('emits imports',                () => expect(generateLean(mod([]))).toContain('import TSLean.Runtime.Basic'));
  it('deduplicates imports', () => {
    const m = mod([]);
    m.imports.push({ module: 'TSLean.Runtime.Basic' });
    const code = generateLean(m);
    expect((code.match(/import TSLean.Runtime.Basic/g) ?? []).length).toBe(1);
  });
});

// ─── Structures ───────────────────────────────────────────────────────────────

describe('generateLean – structures', () => {
  it('basic structure', () => {
    const code = generateLean(mod([{
      tag: 'StructDef', name: 'Point', typeParams: [],
      fields: [{ name: 'x', type: TyFloat }, { name: 'y', type: TyFloat }],
      deriving: ['Repr', 'BEq'],
    }]));
    expect(code).toContain('structure Point');
    expect(code).toContain('x : Float');
    expect(code).toContain('y : Float');
    expect(code).toContain('deriving Repr, BEq');
  });

  it('structure with type params', () => {
    const code = generateLean(mod([{
      tag: 'StructDef', name: 'Box', typeParams: [tp('T')],
      fields: [{ name: 'val', type: TyRef('T') }],
    }]));
    expect(code).toContain('structure Box');
    expect(code).toContain('(T : Type)');  // structs use explicit params
    expect(code).toContain('val : T');
  });

  it('struct inside namespace: fields appear after mk ::', () => {
    const code = generateLean(mod([{
      tag: 'Namespace', name: 'Geo', decls: [{
        tag: 'StructDef', name: 'Rect', typeParams: [],
        fields: [{ name: 'w', type: TyFloat }, { name: 'h', type: TyFloat }],
        deriving: [],
      }],
    }]));
    const lines = code.split('\n');
    const mkIdx = lines.findIndex(l => l.trimEnd().endsWith('mk ::'));
    const wIdx  = lines.findIndex(l => l.includes('w : Float'));
    expect(mkIdx).toBeGreaterThan(-1);
    expect(wIdx).toBeGreaterThan(-1);
    // Fields must appear after mk ::
    expect(wIdx).toBeGreaterThan(mkIdx);
    // Fields must be indented at least as much as the structure keyword
    const structIdx = lines.findIndex(l => l.includes('structure Rect'));
    const structInd = lines[structIdx].match(/^(\s*)/)?.[1].length ?? 0;
    const wInd      = lines[wIdx].match(/^(\s*)/)?.[1].length ?? 0;
    expect(wInd).toBeGreaterThanOrEqual(structInd);
  });
});

